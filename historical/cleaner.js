/**
  Проверяет, мог ли человек иметь email (задел на будущее).
  Логика: email стал общедоступным примерно в 1990 году (регистрация домена .ru в 1994,
  массовые сервисы с 1995 г.). Предполагаем, что человек начал пользоваться почтой не раньше 20 лет.
  Поэтому если год рождения birthYear + 20 >= 1990, то он потенциально мог застать эпоху email.
  Пример: родился в 1970 → 1970+20=1990 → мог; родился в 1965 → 1965+20=1985 → не мог.

  @param {number} birthYear - Год рождения.
  @returns {boolean} - true, если мог иметь email.
*/
function couldHaveEmail(birthYear) {
  return birthYear + 20 >= 1990;
}

/**
  проверяет, мог ли человек иметь телефон (задел на будущее).
  Логика: телефон начал массово распространяться с ~1910 года (первые станции в Москве и СПБ в 1882, но доступно стало гораздо позже).
  Считаем, что тогда человек мог получить телефон во взрослом возрасте (около 30-ти лет).
  Поэтому (год рождения birthYear + 30 >= 1910).
  Пример: родился в 1880 → 1880+30=1910 → мог; родился в 1970 → 1870+30=1900 → не мог.

  @param {number} birthYear - Год рождения.
  @returns {boolean} - true, если мог иметь телефон.
*/
function couldHavePhone(birthYear) {
  return birthYear + 30 >= 1910;
}

/**
  ГЛАВНАЯ ФУНКЦИЯ ОЧИСТКИ.
  Принимает исходного пользователя и возвращает его копию с исправлениями email, телефоном и профессией
  в зависимости от года его активности, а также добавляет статус жизни с учётом пола.

  @param {object} user - Объект пользователя (должен содержать birthDate, deathDate, city, email, phone, profession).
  @returns {object} - Очищенный объект (копия).
*/
function cleanHistoricalData(user) {
  // --- 1. Парсим дату рождения. Если дата не корректная - возвращаем исходный объект как есть.
  /**
    Логика:
    - Вызываем parseDate() для преобразования строки birthDate в объект Date.
    - Если дата некорректна (birthDate = null/false), прерываем обработку,
      чтобы избежать ошибок в последующих шагах.
  */
  const birthDate = parseDate(user.birthDate);
  if (!birthDate) {
    console.warn('⚠️ cleanHistoricalData: некорректная дата рождения, пользователь не обработан');
    return user;
  }

  // --- 2. Создаём КОПИЮ пользователя, чтобы не испортить оригинал.
  /**
    Используем оператор spread ({...user}), чтобы создать поверхностную копию объекта.
    Это гарантирует, что исходный user останется неизменным, а всё изменения будут в cleanedUser.
  */
  const cleanedUser = {...user};

  // --- 3. Если пол не указан, по умолчанию считаем мужским (для склонения).
  /**
    Назначение:
    - Пол (gender) требуется для корректного склонения слов(например, статус "жив"/"жива").
    - Устанавливаем значение по умолчанию 'male', чтобы избежать ошибок при отсутствии данных.
  */
  if (!cleanedUser.gender) {
    cleanedUser.gender = 'male';
  }

  // --- 4. Обрабатываем дату смерти и формируем статус жизни (с учётом пола).
  const deathDate = parseDate(user.deathDate); // Парсим дуту смерти аналогично дате рождения
  const today = new Date(); // Текущая дата для сравнения с датой смерти

  let lifeStatusText; // Переменная для хранения текстового статуса (например, "Жив", "Умер: 31-12-2020")
  // Если дата смерти не указана - человек жив.
  if (!deathDate) {
    /**
      getAliveStatus() возвращает строку, зависящую от пола:
      - 'male' → "Жив"
      - 'female' → "Жива"
    */
    lifeStatusText = getAliveStatus(cleanedUser.gender);
    // Если дата смерти в будущем (например, осужденный на казнь с назначенной датой).
  } else if (deathDate > today) {
    /**
      Форматируем дату смерти через formatDate(), чтобы вывести её в читаемом виде
      (например, «2026-12-31» → «31 декабря 2026»).
    */
    const aliveWord = getAliveStatus(cleanedUser.gender); // Текущий статус («Жив»/«Жива»)
    lifeStatusText = `${aliveWord} (ожидаемая смерть:) ${formatDate(user.deathDate)}`;
  } else {
    // Иначе дата смерти в прошлом - человек умер.
    /**
      getDiedStatus() аналогично getAliveStatus(), но возвращает слово «Умер»/«Умерла».
      Формат вывода: «Умер: 15 мая 1945».
    */
    const diedWord = getDiedStatus(cleanedUser.gender); // «Умер»/«Умерла»
    lifeStatusText = `${diedWord}: ${formatDate(user.deathDate)}`;
  }
  // Добавляем статус вочищенный объект
  cleanedUser.lifeStatusText = lifeStatusText;

  // --- 5. Определяем исторический период по году рождения.
  /**
    getHistoricalPeriod() классифицирует год рождения в исторический период, например:
    - до 1000 г. → "Древний мир"
    - 1400-1600 → "Эпоха возрождения"
    - 1917-1991 → "Советский период"
    Результат сохраняется в cleanedUser.historicalPeriod.
  */
  const birthYear = birthDate.getFullYear(); // Извлекаем год из объекта birthDate
  cleanedUser.historicalPeriod = getHistoricalPeriod(birthYear);

  // --- 6. определяем "последний активный год".
  // Если человек умер - год смерти, иначе текущий год.
  /**
    Логика:
    - Если deathDate существует → пользователь умер → последний активный год = году смерти.
    - Иначе → пользователь жив → последний активный год = текущему году.
    Цель:
    - Используется для внвлиза активности (например, для расчёта продолжительности жизни или периода,
    в течение которого пользователь был активен).
  */
  const lastActiveDate = deathDate || new Date();
  const lastActiveYear = lastActiveDate.getFullYear();
  const lastActiveMonth = lastActiveDate.getMonth() + 1; // январь = 1
  // --- Подсчитаем возраст на последний активный день ---
  const ageDetails = calculateAgeDetailed(user.birthDate, lastActiveDate);
  const currentAgeInYears = ageDetails.years;


  // ---------- ОБРАБОТКА EMAIL ----------
  /**
    Стратегия:
    - Если в исходных данных есть реалистичный email (не пустой и не содержит "exemple"),
      и последний активный год >= 1995 (когда электронная почта стала массово распространяться),
      то сохраняется исходный email.
    - в противном случае генерится "исторический email", имитирующий адреса, характерные для денного периода.
  */

  // ---------- БЛОКИРОВКА ДАННЫХ ДЛЯ МАЛЫШЕЙ ----------
  // Дети до 8 лет ещё не могут иметь email, телефон и профессию
  if (currentAgeInYears < 8) {
    cleanedUser.email = "нет";
    cleanedUser.phone = "нет";
    cleanedUser.profession = "нет";
    // Профессию не обрабатываем дальше, сразу переходим к возрасту
    return cleanedUser;
  }

  if (user.email && user.email !== "" && !user.email.includes("exemple")) {
    if (lastActiveYear >= 1995) {
      // Если пользователь активен в эпоху массовой почты - оставляем реальный email
      cleanedUser.email = user.email;
    } else {
      // Иначе для периодов до 1995 генерируем условный исторический email.
      // Например, для исторических персон или ранних периодов.
      cleanedUser.email = generateHistoricalEmail(lastActiveYear, user.name);
    }
  } else {
    // Если email пуст, содержит "example" или не прошёл проверку - генерируем исторический
    cleanedUser.email = generateHistoricalEmail(lastActiveYear, user.name);
  }
  /**
  Примечания:
  - Проверка на "example" отсеивает тестовые/демонстрационные адреса вроде example@example.com.
  - generateHistoricalEmail() создаёт условный адрес, возможно, в формате:
    - [имя].[фамилия]@[год].com (для условно «современных» исторических периодов)
    - или более архаичные варианты (зависит от реализации функции).
  */
  
  // ---------- ОБРАБОТКА ТЕЛЕФОНА ----------
  // Стратегия:
  // - Учитываем исторические вехи развития телефонов.
  // - Формируем номер в зависимости от периода активности пользователя.
  // 1876 г. - до этого года телефон не был изобретён
  if (lastActiveYear < 1876) {
    cleanedUser.phone = "телефон ещё не был изобретён";
  } else if (lastActiveYear < 1910) {
    // Ранний период телефонии (1876-1910):
    // - Номера были короткими, часто без кодов. Генерируем примерное число (от 100 до 999).
    /**
      Пояснение:
      - Используется Math.random() для имитации случайного трёхзначного номера.
      - Формат упрощён, так как в те годы стандарты номеров не были устоявшимися.
    */
    cleanedUser.phone = `Ранний телефонный номер (пример: ${Math.floor(Math.random() * 900) + 100})`;
  } else if (lastActiveYear < 1993) {
    cleanedUser.phone = getHistoricalPhoneNumber(lastActiveYear, user.city, lastActiveMonth);
    // Исторический период (1910-1992):
    /**
      Номера формировались с учётом региона/города.
      getHistoricalPhoneNumber() генерирует номер, учитывающий город и год.
      Пример логики getHistoricalPhoneNumber().
      - Для Москвы (1920-е): 2-34-56
      - Для провинциального города (1950-е): код региона + 5-ти значный номер
    */
  } else {
    // Современность (1993+):
    // - Если телефон указан - сохраняем.
    // - Если не указан - Генерируем современный номер, привязанный к городу.
    /**
      getHistoricalPhoneNumber() в этом режиме:
      - Может генерировать номер в международном формате (+7 ХХХ ХХХ-ХХ-ХХ)
      - Учитывать региональные коды, характерные для user.city
    */
    if (!user.phone || user.phone === "") {
      cleanedUser.phone = getHistoricalPhoneNumber(lastActiveYear, user.city, lastActiveMonth);
    } else {
      // иначе сохраняем реальный номер, если он есть в исходных кодах
      /**
        Обоснование выбора 1993 года:
        - примерно с этого периода времени началась массовая стандартизация номеров,
        распространение сотовых операторов в России и других странах
      */
      cleanedUser.phone = user.phone;
    }
  }

  // ---------- ОБРАБОТКА ПРОФЕССИИ ----------

  // Заменяем современное название профессии на исторический аналог в зависимости от года смерти
  // Логика: для каждой современной профессии определена функция, которая по периоду активности (lay)
  // возвращает соответствующий исторический термин, учитывающий технологические и социальные реалии эпохи.
  const professionMap = {
    "Разработчик": (lay) => {
      // Эволюция термина:
      // - До 1950: профессия ассоциировалась с математикой (первые вычислительные алгоритмы)
      if (lay < 1950) return "Математик";
      // - 1950-1970: эра первых ЭВМ
      else if (lay < 1970) return "Инженер-электронщик";
      // - 1970-1990: появление термина "программист"
      else if (lay < 1990) return "Инженер-программист";
      // - c 1990-х лет: современный термин "разработчик"
      else return "Разработчик";
    },
    "Дизайнер": (lay) => {
      // Эволюция термина:
      // - До 1800 это был просто термин "художник"
      if (lay < 1800) return "Художник";
      // - 1800-1920: переход к декораторским работам
      else if (lay < 1920) return "Декоратор";
      // - 1920-1960: специализация как художник-оформитель
      else if (lay < 1960) return "Художник оформитель";
      // - 1960-1990: термин "график" (связь с печатной графикой)
      else if (lay < 1990) return "График";
      // - c 1990: современный термин
      else return "Дизайнер";
    },
    "Маркетолог": (lay) => {
      // Эволюция термина:
      // - До 1800: базовый термин "торговец"
      if (lay < 1800) return "Торговец";
      // - 1800-1920: купеческая деятельность
      else if (lay < 1920) return "Купец";
      // - 1920-1960: товароведческий контроль
      else if (lay < 1960) return "Товаровед";
      // - 1960-1990: Экономист по сбыту
      else if (lay < 1990) return "Экономист по сбыту";
      else return "Маркетолог";
    },
    "Аналитик": (lay) => {
      // Эволюция термина:
      // - до 1800: был философский подход к анализу
      if (lay < 1800) return "Философ";
      // - 1800-1920: был статистический анализ
      else if (lay < 1920) return "Статистик";
      // - 1920-1960: это уже был экономический анализ
      else if (lay < 1960) return "Экономист";
      // - 1960-1990: системный аналитик в IT
      else if (lay < 1990) return "Системный аналитик";
      // - c 1990: уже просто по современному - Аналитик
      else return "Аналитик";
    },
    "Менеджер": (lay) => {
      // Эволюция термина:
      // - до 1900 года Это был управляющий домом или хозяйством
      if (lay < 1900) return "Управляющий";
      // - 1900-1960: начальник отдела
      else if (lay < 1960) return "Начальник отдела";
      // - 1960-1990: руководитель (более общий термин)
      else if (lay < 1990) return "Руководитель";
      // - с 1990: уже современный термин "менеджер"
      else return "Менеджер";
    },
    "Врач": (lay) => {
      // Эволюция термина:
      // - с до 1800: "лекарь" (народная медицина)
      if (lay < 1800) return "Лекарь";
      // - 1800-1920 "доктор" (уже современный научный подход)
      else if (lay < 1920) return "Доктор";
      // - c 1920: современный термин "врач"
      else return "Врач";
    },
    "Учитель": (lay) => {
      // Эволюция термина:
      // - До 1920 Учитель всегда назывался учителем до современного периода - преподаватель
      if (lay < 1920) return "Преподаватель";
      else return "Учитель";
    },
    "Строитель": (lay) => {
      // Эволюция термина:
      // - До 1600: средневековые мастера.
      if (lay < 1600) return "Ремесленник-каменщик";
      // - 1600-1800: специализация по материалам
      else if (lay < 1800) return "Каменщик / Плотник";
      // - 1800-1950: период индустриализации
      else if (lay < 1950) return "Мастер строительных работ";
      // - c 1950: современный уже термин
      else return "Строитель";
    },
    "Фермер": (lay) => {
      // Эволюция термина:
      // - До 1700: до аграрных реформ - крестьюнство
      if (lay < 1700) return "Крестьянин";
      // - 1700-1930: переход к рыночному хозяйству
      else if (lay < 1930) return "Земледелец";
      // - c 1930: современный термин, особенно после коллективизации/разгосударствления
      else return "Фермер";
    },
    "Водитель": (lay) => {
      // Эволюция термина:
      // - До 1900: лошадиные экипажи
      if (lay < 1900) return "Кучер";
      // - 1900-1950: первые автомобили - основание автомобилизма.
      else if (lay < 1950) return "Шофёр";
      // - c 1950: уже современный унифицированный термин
      else return "Водитель";
    },
    "Почтальон": (lay) => {
      // Эволюция термина:
      // - До 1600: пешие, конные посланники
      if (lay < 1600) return "Гонец";
      // - 1600-1900: период регулярной почтовой службы
      else if (lay < 1900) return "Почтовый курьер";
      // - с 1900: современный термин
      else return "Почтальон";
    },
    "IT-специалист": (lay) => {
      // Эволюция термина:
      // - До 1960: эпоха ранних компьютеров
      if (lay < 1960) return "Инженер ЭВМ";
      // - 1960-1990: эпоха майнфреймов - высокопроизводительных компьютеров
      //   большие универсальные ЭВМ общего назначения с мощным железом (серверы)
      else if (lay < 1990) return "Программист-системщик";
      // - с 1990: уже современный обобщённый термин
      else return "IT-специалист";
    },
    "Графический дизайнер": (lay) => {
      // Эволюция термина:
      // - До 1920: ручная графика зарисовщики
      if (lay < 1920) return "Художник-илюстратор";
      // - 1920-1980: Офсетная печать основана на переносе изображения с печатных форм на бумагу через офсетный цилиндр.
      else if (lay < 1980) return "Полиграфический художник";
      // - с 1980: цифровая эра (термин закрепился с 1980-х, даже при сохранившемся офсете)
      else return "Графический дизайнер";
    }
  };

  // Проверяем, есть ли современная профессия в словаре исторических аналогов.
  // Если есть - вызываем функцию, передавая период активности (lastActiveYear),
  // и заменяем современное название профессии на соответствующий исторический аналог, если этого требует период времени.
  if (professionMap[cleanedUser.profession]) {
    cleanedUser.profession = professionMap[cleanedUser.profession](lastActiveYear);
  }

  // ---------- ВОЗРАСТНЫЕ СТАТУСЫ ДЛЯ ДЕТЕЙ И ПОДРОСТКОВ ----------
  // Если профессия отсутствует или была сброшена, определяем статус по возрасту
  if (!cleanedUser.profession || cleanedUser.profession === "") {
    if (currentAgeInYears < 8) {
      // сюда мы не попадём из-за раннего return, но оставим для страховки
      cleanedUser.profession = "нет";
    } else if (currentAgeInYears < 16) {
      cleanedUser.profession = "школьник";
    } else if (currentAgeInYears < 18) {
      cleanedUser.profession = "студент";
    } else {
      cleanedUser.profession = "безработный";
    }
  }
  
  return cleanedUser; // Возвращает очищенный объект с преобразованными данными
}