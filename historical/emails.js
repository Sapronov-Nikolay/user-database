/**
  Собирает полный массив доменов, уже доступных к указанному году.
  Проходит по массиву DOMAIN_ADDITIONS и накапливает все newDomains,
  у которых startYear <= activeYear. Таким образом, мы не дублируем
  перечисление ранее добавленных доменов в каждом году.

  @param {number} activeYear - Год,для которого нужно собрать список доменов.
  @returns {strung[]} - Массив доменов, доступных в указанном году. Достаём их по индексу.
*/
function getAvilableDomian(activeYear) {
  // Пустой список для заполнения данными
  const domians = [];

  // Перебираем эпохи в порядке возрастания startYear
  for (const addition of DOMIANS_ADDITIONS) {
    // Если эпоха уже началась (её год не позже activeYear), добавляем все домены в общий список
    if (addition.startYear <= activeYear) {
      // оператор ... (spread) добавляет в указанный массив состав другого массива.
      domians.push(...addition.newDomians);
    } else {
      // Как только встречаем эпоху, которая ещё не наступила, то останавливаем перебор.
      break;
    }
  }
  // Выводим то что вложили ...spread-оператором
  return domians;
}

// ---------- ГЕНЕРАТОР EMAIL ----------

/**
  Генерирует email, который мог существовать в указанный год.
  Если год слишком ранний, выводит информационное сообщение.
  В остальных случаях формирует адрес из имени пользователя и случайного домена, доступного в указанный год.

  @param {string} baseName - Основа для имени (обычно имя пользователя).
  @returns {string} - Email или информационное сообщение.
*/
function generateHistoricalEmail(activeYear, baseName) {
  // До 1971 года электронной почты ещё не было
  if (activeYear < 1971) {
    return "Электронная почта ещё не была изобретена";
  }

  // С 1971 по 1995 год email уже был изобретён, но массового доступа не было.
  if (activeYear < 1995) {
    return "Ранняя электронная почта (ещё не распространена и почти ни укого нет)";
  }

  // Формируем часть до собачки
  // - Убираем лишние пробелы (на случай случайных пробелов или двойного имени)
  // - добавляем две последние цифры года, чтобы снизить вероятность совпадений
  const username = baseName.replace(/\s+/g, '') + (activeYear);
  // Получаем все домены, уже существующие к указанному году.
  const availableDomians = getAvilableDomian(activeYear);

  // Если по какой-то причине список пуст (например, год меньше 1996 года), выводим поясняющее сообщение.
  if (availableDomians.length === 0) {
    return "Нет доступных почтовых сервисов для этого периода лет";
  }

  // Случайно выбираем один из доступных доменов.
  const domian = availableDomians[Math.floor(Math.random() * availableDomians.length)];
  // Собираем итоговый email и выводим
  return `${username}@${domian}`;
}

// ---------- ПРОВЕРКА ДОСТОВЕРНОСТИ ДОМЕНА ДЛЯ ГОДА ----------

/**
  Проверяет, мог ли существовать указанный email в заданный год.
  Возвращает строку с предупреждением, если домен ещё не существовал, или пустую строку, если всё в порядке.

  @param {string} email - Полный email-адрес.
  @param {number} activeYear - Год, в который проверяется возможность существования email.
  @returns {string} - Пустая строка, если всё корректно, иначе предупреждение.
*/
function validateHistoricalEmail(email, activeYear) {
  // Извлекаем домен из адреса почты (то, что после '@').
  // приводим к нижнему регистру для единообразия и надёжности
  const domian = email.split('@')[1]?.toLowerCase();
  // Если домен не удалось извлечь - проверка не требуется.
  if (!domian) return '';

  // получаем список доменов, доступных в указанный год.
  const availableDomians = getAvilableDomian(activeYear);

  // Если домен отсутствует в списке доступных - значит, он ещё не существовал в тот год.
  if (!availableDomians.includes(domian)) {
    // Тогда определяем, когда этот домен впервые появился, чтобы информативнее об этом сообщить
    let introducedYear = null;
    for (const addition of DOMIANS_ADDITIONS) {
      if (addition.newDomians.includes(domian)) {
        introducedYear = addition.startYear;
        break; // Если домен есть в указанный период, то дальше не ищем.
      }
    }
    // Форматируем предупреждающее сообщение
    const introMsg = introducedYear ? ` (появился в ${introducedYear})` : '';
    return `⚠️ Домен @${domian} не существовал в ${activeYear} году.${introMsg}`;
  }
  // Если домен есть в списке - всё хорошо.
  return '';
}

