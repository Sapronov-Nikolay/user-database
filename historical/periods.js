/**
  Определяет исторический период по году рождения.
  Сохраняет хронологическую структуру в комментарии.
  Возвращает строку-название из PERIOD_TITLES.

  @param {number} birthYear - Год рождения.
  @returns {string} - Название периода с краткой технологической пометкой о нём.
*/
function getHistoricalPeriod(birthYear) {
  // --- 1. ДОИНДУСТРИАЛЬНАЯ ЭПОХА (до XIX века) ---
  if (birthYear < 0) {
    return PERIOD_TITLES.UNTIL_0;
  }
  if (birthYear < 500) {
    return PERIOD_TITLES.UNTIL_500;
  }
  if (birthYear < 1500) {
    return PERIOD_TITLES.UNTIL_1500;
  }
  if (birthYear < 1700) {
    return PERIOD_TITLES.UNTIL_1700;
  }
  if (birthYear < 1800) {
    return PERIOD_TITLES.UNTIL_1800;
  }

  // --- 2. XIX ВЕК (до и после изобретения телефона Александром Беллом в 1876 г.) ---
  if (birthYear < 1876) {
    return PERIOD_TITLES.UNTIL_1876;
  }
  if (birthYear < 1900) {
    return PERIOD_TITLES.UNTIL_1900;
  }

  // --- 3. НАЧАЛО XX ВЕКА (Российская империя до революции 1917) ---
  if (birthYear < 1917) {
    return PERIOD_TITLES.UNTIL_1917;
  }
  if (birthYear < 1917) {
    return PERIOD_TITLES.UNTIL_1917;
  }

  // --- 4. СОВЕТСКАЯ ЭПОХА (1917–1991) ---
  if (birthYear < 1929) {
    return PERIOD_TITLES.UNTIL_1929;
  }
  if (birthYear < 1941) {
    return PERIOD_TITLES.UNTIL_1941;
  }
  if (birthYear < 1945) {
    return PERIOD_TITLES.UNTIL_1945;
  }
  if (birthYear < 1953) {
    return PERIOD_TITLES.UNTIL_1953;
  }
  if (birthYear < 1964) {
    return PERIOD_TITLES.UNTIL_1964;
  }
  if (birthYear < 1985) {
    return PERIOD_TITLES.UNTIL_1985;
  }
  if (birthYear < 1991) {
    return PERIOD_TITLES.UNTIL_1991;
  }

  // --- 5. ПОСТСОВЕТСКАЯ РОССИЯ (с 1991) ---
  if (birthYear < 2000) {
    return PERIOD_TITLES.UNTIL_2000;
  }
  if (birthYear < 2009) {
    return PERIOD_TITLES.UNTIL_2009;
  }
  if (birthYear < 2019) {
    return PERIOD_TITLES.UNTIL_2019;
  }

  // --- 6. НАШИ ДНИ (после 2020) ---
  return PERIOD_TITLES.AFTER_2019;
};

/**
  Показывает всплывающее сообщение окно с подробным описанием исторического периода.
  Окно появляетсяпо центру экрана и закрывается кликом в любом месте вне его.
  Используется для поиска текстового описания в справочнике historicalPeriodDescriptions.

  @param {string} period - Название периода (значение из PERIOD_TITLES - ключ в объекте historicalPeriodDescriptions).
  */

let activePopup = null; // Ссылка на текущее активное всплывающее окно (null, если окна нет)

function showPeriodDescription(period) {
  // Удаляем предыдущее окно, если оно есть - предотвращает наложение нескольких попапов
  if (activePopup) {
    activePopup.remove(); // Удаляем элемент из DOM
    activePopup = null;   // Сброс ссылки на активное окно
  }

  // Получаем текстовое описание периода из глобального объекта historicalPeriodDescriptions
  // Запускаем функцию. Если она ничего не найдёт для отображения, то выдаст дежурное сообщение.
  const desc = historicalPeriodDescriptions[period] ||
    "Информация об этом периоде пока не добавлена в справочник.";

  // Создаём структуру HTML для всплывающего окна
  const popup = document.createElement('div');  // Контейнер всплывающего окна
  popup.className = 'period-popup'; // CSS-класс для стиллизации

  const title = document.createElement('div');  // Заголовок окна
  title.className = 'period-popup__title';  // CSS-сласс заголовка
  title.textContent = `📜 ${period}`; // Текст заголовка: эмодзи + название периода

  const descEl = document.createElement('div'); // Область с описанием
  descEl.className = 'period-popup__desc';  // CSS-класс содержимого
  descEl.textContent = desc;  // Текст описания (из historicalPeriodDescriptions)

  // Собираем конструкцию окна.
  popup.appendChild(title);   // Собираем заголовок
  popup.appendChild(descEl);  // Собираем описание
  document.body.appendChild(popup); // Добавляем всё в DOM

  // Сохраняем ссылку на созданное окно, чтобы управлять им позже (например, закрыть)
  activePopup = popup;

  // Функциядля закрытия окна при клике вне его
  const closeHandler = (e) => {
    // Если клик был внутри окна (popup.contains(e.target) = true) - игнорируем событие
    if (popup.contains(e.target)) return;
    
    // Иначе:
    popup.remove(); // Удаляем окно из DOM
    activePopup = null; // Обнуляем ссылку на удалённое окно
    document.removeEventListener('click', closeHandler); // Отписываемся от события клика
  };

  // Задержка в 10 мс нужна, чтобы:
  // 1. Клик по самому попапу не вызвал его мгновенное закрытие
  // 2. Дать время браузеру отобразить окно перед установкой обработчика клика
  setTimeout(() => {
    document.addEventListener('click', closeHandler);
  }, 10);
}