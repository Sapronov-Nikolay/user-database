// Работа с историческими периодами, телефонами, анахронизмами.

// Эти функции очищают анахронизмы (современные данные) для исторических личностей
// Убирает email, телефон и корректирует проффессии в зависимости от эпохи
/*
  Определяет исторический период по году рождения.
  Обновлено с учётом технологических эпох (телефон, email) и общепринятой
  периодизации истории России/СССР (Оттепель, Застой, Перестройка и т.д.).
  @param {number} birthYear - год рождения
  @returns {string} - название периода
*/

function getHistoricalPeriod(birthYear) {
  // === 1. ДОИНДУСТРИАЛЬНАЯ ЭПОХА ===
  // Ни телефона, ни электричества. Связь — только письма и гонцы.
  if (birthYear < 0) return "До нашей эры (ни телефона ни почты, только гонцы)";
  if (birthYear < 500) return "Античность (гонцы и голуби)";
  if (birthYear < 1500) return "Средневековье (рукописные письма)";
  if (birthYear < 1700) return "Эпоха Возрождения (книгопечатание, почта)";
  if (birthYear < 1800) return "Эпоха Просвещения (регулярная почта)";

  // === 2. XIX ВЕК (до изобретения телефона) ===
  // Телефон изобретён Александром Беллом в 1876 году. Люди, рождённые до этого, в дотелефонную эпоху.
  if (birthYear < 1876) return "XIX век (дотелефонная эпоха)";
  // 1882 г. – первые телефонные станции в Москве и Петербурге. Телефон — роскошь для избранных.
  if (birthYear < 1900) return "конец XIX века (заря телефонии, телефон — роскошь)";

  // === 3. XX ВЕК НАЧАЛО (Российская империя) ===
  // Период до революции 1917 года. Телефон - роскош, есть только в крупных городох.
  if (birthYear < 1917) return "Начало XX века (российская империя, телефон у избранных)";

  // === 4. СОВЕТСКАЯ ЭПОХА (1917-1991) ===
  // Разделена на общепринятые исторические периоды
  // Восстановление после Гражданской войны. Телефонизация только начинается.
  if (birthYear < 1929) return "1920-е: НЭП и образование СССР (телефон — редкость)";
  // Индустриализация. Телефон появляется на предприятиях и в учреждениях.
  if (birthYear < 1941) return "1930-е: Индустриализация и коллективизация (телефон на работе)";
  // Война. Разрушение сетей связи, активное использование полевых телефонов.
  if (birthYear < 1945) return "1940-е: Великая Отечественная Война (полевые телефоны)";
  // Восстановление страны. Начало массовой телефонизации городов.
  if (birthYear < 1953) return "Послевоенный сталинизм (1945-1953) (телефон в некоторых квартирах, но ещё редкость)";
  // "Хрущёвская оттепель" (1953-1964). Телефон становится доступнее, появляется в квартирах.
  if (birthYear < 1964) return "Хрущёвская оттепель (1953-1964) (домашний телефон)";
  // "Эпоха застоя" (1964-1985). В этот период появляется первая электронная почта (1971).
  if (birthYear < 1985) return "Эпоха застоя (1964-1985) (телефон + первые email)";
  // "Перестройка" (1985-1991). Гласность, перемены. Первые шаги к цифровизации.
  if (birthYear < 1991) return "Перестройка (1985-1991) (телефон, первые компьютеры)";

  // === 5. ПОСТСОВЕТСКАЯ РОССИЯ (с 1991) ===
  // «Лихие девяностые» (1991–1999) – период рыночных реформ, кризиса и бандитизма.
  // 1991 г. – первый звонок по сотовому телефону в России (мэр Собчак).
  // 1994 г. – регистрация домена .ru, рождение Рунета.
  // 1998 г. – появление Mail.ru.
  if (birthYear < 2000) return "Лихие девяностые (1991-1999) (первые мобильники, email)";
  // «Нулевые» (2000–2009) – период стабилизации и экономического роста.
  // Массовое распространение мобильной связи и домашнего интернета.
  // 2006 г. – количество SIM-карт превысило население России.
  // 2007 г. – запуск 3G, начало эры мобильного интернета.
  if (birthYear < 2009) return "Нулевые годы XXI века (2000-2009) (мобильный интернет)";
  // «Десятые» (2010–2019) – эра смартфонов, соцсетей и цифровой трансформации.
  // Взрывной рост мобильного интернета, мессенджеров (WhatsApp, Telegram).
  // Связь становится неотъемлемой частью повседневной жизни.
  if (birthYear < 2019) return "Десятые годы XXI века (2009-2019) (смартфоны, мессенджеры)";
  // === 6. НАШИ ДНИ ===
  // Для тех, кто родился после 2020 года.
  // Эра 5G, искусственного интеллекта и «интернета вещей».
  return "XXI век (Современность: 5G, ИИ)";
}

