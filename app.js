// Всё, что рассчитывает возраст (годы, месяцы, дни, подробный возраст).

// Вычисляет возраст в годах, месяцах и днях
// Точный расчёт с учётом високосных лет
// Пример: если сегодня 15 марта 2024, а родился 20 февраля 2010 результат: {years: 14, months: 0, days: 23}
// @param {string} birthDateStr - строка с датой рождения
// @return {object} - объект с годами, иесяцами и днями


// Вычисляет возраст человека в годах на основе даты рождения
// Теперь корректно обрабатывает будущие даты (возвращает 0 или предупреждение)
// @param {string} birthDateStr - строка с датой рождения (например "02-01-2027")
// @returns {number} - возраст в годах (не может быть отрицательным)
function calculateAge(birthDateStr) {
  // 1. парсим строку даты в объект Date
  const birthDate = parseDate(birthDateStr);
  // 2.Если парсинг не удался (некорректная дата), возвращаем 0
  if (!birthDate) return 0;
  // 3. Получаем текущую дату для сравнения
  const today = new Date();

  // 4. ПРОВЕРКА НА БУДУЩУЮ ДАТУ: если дата позже сегодняшней
  //    Это важно, чтобы не показывать отрицательный возраст
  if (birthDate > today) {
    // Выводим предупреждение в консоль для разработчика
    console.warn(`⚠️ calculateAge: Дата рождения ${birthDateStr} находится в будущем`);
    return 0;   // Возвращаем 0 вместо отрицательного числа
  }

  //  5. ОСНОВНОЙ РАСЧЁТ: разница между текущем годом и кодом рождения
  //     Например: 2024 - 2000 = 24 года
  let age = today.getFullYear() - birthDate.getFullYear();

  // 6. ПРОВЕРКА ДНЯ РОЖДЕНИЯ В ТЕКУЩЕМ ГОДУ:
  //    Возраст увеличивается только после дня рождения в текущем году
  const hasBirthDayPassed = (
    // Сравниваем месяцы: если текущий месяц больше месяца рждения
    today.getMonth() > birthDate.getMonth() ||
    // ИЛИ если месяцы одинаковые, но сегодняшний день >= дню рождения
    (today.getMonth() === birthDate.getMonth() && today.getDate() >= birthDate.getDate())
  );
  
  // 7. КОРРЕКТИРОВКА: если день рождения ещё не наступил в этом году
  //    вычитаем 1 из возраста
  //    Пример: родился 15 декабря 2000, сегодня 1 ноября 2024
  //    Разница лет: 2024-2000=24, но день рождения ещё не наступил -> ещё 23 года
  if (!hasBirthDayPassed) {
    age--;
  }

  // 8. ГАРАНТИЯ НЕОТРИЦАТЕЛЬНОСТИ: Math.max(0, age) возвращает
  //    либо age, если он положительный, либо 0, если age отрицательный
  //    Это дополнительная защита от странных случаев
  return Math.max(0, age);
}


function calculateAgeDetailed(birthDateStr, endDate = new Date()) {
  const birthDate = parseDate(birthDateStr);
  if (!birthDate) 
    return {
      years: 0,
      months: 0,
      days: 0
    };

  // Защита: если endDate раньше рождения
  if (endDate < birthDate) {
    return { 
      years: 0,
      months: 0,
      days: 0
     };
  }

  // Вычисляем разницу в годах, месяцах и днях
  let years = endDate.getFullYear() - birthDate.getFullYear();
  let months = endDate.getMonth() - birthDate.getMonth();
  let days = endDate.getDate() - birthDate.getDate();

  // Если месяц рождения еще не наступил в этом году
  // ИЛИ если тот же месяц, но день рождения еще не наступил
  if (months < 0 || (months === 0 && days < 0)) {
    years--;          // Уменьшаем годы на 1
    months += 12;     // Добавляем 12 месяцев
  }

  // Если дни отрицательные (день рождения в текущем месяце еще не наступил)
  if (days < 0) {
    // Получаем количество дней в предыдущем месяце
    // new Date(год, месяц, 0) создаёт дату последнего дня предыдущего месяца
    const lastDayOfPrevMonth = new Date(endDate.getFullYear(), endDate.getMonth(), 0);
    days += lastDayOfPrevMonth.getDate();
    months--;   // Минусуем 1 месяц
  }

  // Если месяцы стали отрицательными после корректировки дней
  if (months < 0) {
    months += 12;   // Прибавляем 12 месяцев
    years--;        // Минусуем 1 год
  }
  // Выводим дату с обратной формой "ГГГГ-ММ-ДД"
  return { years, months, days };
}

// Возвращает возраст в виде читаемой строки на русском языке
// Обрабатывает все случаи: будущие даты, меньше года, точный возраст
// @param {string} birthDateStr - строка с датой рождения
// @return {string} - возраст в виде строки ("25 лет", "6 месяцев", "Родится через 1 год")
function getAgeStrung(birthDateStr) {
  // 1 парсим дату рождения
  const birthDate = parseDate(birthDateStr);
  // 2. Если дата не корректна, возвращаем сообщение об ошибке
  if (!birthDate) return "Дата рождения не указана";
  // 3. Получаем текущую дату
  const today = new Date();
  // 4. Проверяем, являетсяли дата исторической датой
  if (birthDate.getFullYear() < 1900) {
    const yearsAgo = new Date().getFullYear() - birthDate.getFullYear();
    return `${yearsAgo} ${getWordForm(yearsAgo, "year")} назад (историческая личность)`;
  }

  // ====== сЛУЧАЙ 1: ДАТА РОЖДЕНИЯ В БУДУЩЕМ ======
  // Если человек ещё не родился, показываем, сколько осталось ему до рождения
  if (birthDate > today) {
    // 3.1 Вычисляем разницу во времени в милисекундах
    const timeDiff = birthDate - today;
    // 3.2 Переводим миллисекунды в дни (округление вверх)
    //     1000 мс * 60 сек * 60 мин * 24 часа = 86 400 000 -> количество мс в сутках
    const daysUntilBirth = Math.ceil(timeDiff / (1000 * 60 * 60 * 24));
    // 3.3 Форматируем в зависимости от количества дней:
    if (daysUntilBirth < 30) {
      // Если меньше месяца, то показываем дни - 86,4 млн мс/сут
      return `${daysUntilBirth} ${getWordForm(daysUntilBirth, "day")} до рождения`;
    } else if (daysUntilBirth < 365) {
      // Меньше года: показываем месяцы - 2,630 016 млрд мс/мес (по ср. кол. дней/мес)
      const months = Math.floor(daysUntilBirth / 30.44); // среднее количество дней в месяце
      return `${months} ${getWordForm(months, "month")} до рождения`;
    } else {
      // Больше года: показываем годы - 960,613 344 млрд мс/году (по ср. кол. дней/году)
      const years = Math.floor(daysUntilBirth / 365.25);  // 365.25 - с учётом високосных
      return `${years} ${getWordForm(years, "year")} до рождения`;
    }
  }

  // ====== СЛУЧАЙ 2: ЧЕЛОВЕК УЖЕ РОДИЛСЯ ======
  // 4. Получаем возраст в годах с помощью calculateAge()
  const ageInYears = calculateAge(birthDateStr);
  // 5. Если возраст 0 лет (меньше года), показываем подробнее
  if (ageInYears === 0) {
    // 5.1 Получаем детальный возраст (месяцы и дни)
    const detailedAge = calculateAgeDetailed(birthDateStr);
    // 5.2 Если есть месяцы, показываем их
    if (detailedAge.months > 0) {
      return `${detailedAge.months} ${getWordForm(detailedAge.months, "month")}`;
    } else {
      // Иначе показываем дни (для новорожденных)
      return `${detailedAge.days} ${getWordForm(detailedAge.days, "day")}`;
    }
  }

  // ====== СЛУЧАЙ 3: ВОЗРАСТ 1 ГОД И БОЛЬШЕ ======
  // 6. Получаем правильную форму слова "год/года/лет"
  const ageWord = getWordForm(ageInYears, "age");
  // 7. Возвращаем строку в форме "25 лет"
  return `${ageInYears} ${ageWord}`;
}

