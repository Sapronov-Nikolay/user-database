// ====== ШАГ 0.2: ВСПОМОГАТЕЛЬНЫЕ ФУНКЦИИ ДЛЯ СКЛОНЕНИЯ СЛОВ ======
// ===========================================================================
// Универсальный трафарет для склонения русских существительных
// Применяет число и 3 формы, возвращает правильную форму
// Правила русского языка:
// - 1, 21, 31, 101 → форма 1 (год)
// - 2-4, 22-24, 32-34 → форма 2 (года)
// - 5-20, 25-30, 35-40 → форма 3 (лет)
// Особое исключение: 11-14 (и 111-114, 1011-1014) всегда форма 3 (лет)
// @param {number} number - количкство
// @param {Array} forms - ьассив из 3-х форм: [для 1, для 2-4, для 5-20]
// @returns {string} - правильная форма слова
function pluralForm(number, forms) {
  // Проверяем, что forms - это массив из 3-х элементов
  if (!Array.isArray(forms) || forms.length !== 3) {
    console.error('❌ pluralForm: нужен массив из 3-х форм');
    return forms[2] || '';
  }
  // Преобразуем число (на случай, если передана строка)
  // Math.abs() убирает знак минус, если число отрицательное
  const num = Math.abs(Number(number));
  // Берём последние цифры для анализа
  const lastTwo = num % 100;    // Для последних двух: 11, 12, 13, 14, 111, 1012
  const lastOne = num % 10; // Для последней одной: 2, 3, 4, 5 и т.д. Можно и так: const lastOne = lastTwo % 10;

  // ШАГИ СКЛОНЕНИЙ ПО ПАДЕЖАМ
  // ШАГ 1: ИСКЛЮЧЕНИЕ - числа 11-14 (и 111-114, 1011-1014 и т.д.)
  if (lastTwo >= 11 && lastTwo <= 14) {
    return forms[2];    // Форма для 5-20 (индекс 2 в массиве) -> Лет
  }
  // ШАГ 2: ПРАВИЛО 1-е - окончание на 1 (но не 11!)
  if (lastOne === 1) {
    return forms[0];    // Форма для 1 (индекс 0 в массиве) -> Год
  }
  // ШАГ 3: ПРАВИЛО 2-е - окончание на 2, 3, 4 (но не 12-14!)
  if (lastOne >= 2 && lastOne <= 4) {
    return forms[1];   // форма для 2-4 (индекс 1 в массиве) -> Года
  }
  // ШАГ 4: ПРАВИЛО 3-е - все остальное (5-20, 25-30 и т.д.)
  return forms[2];    // Форма для 5-20 (индекс 2 в массиве) -> Лет
}

// Библиотека готовых форм дляразных слов (можно расширять)
// Должны быть с массивом из трёх значений - вариантов произношения в склонении по падежам
// Модель массива: массив {ключ: [значение[0], [значение[1], [значение[2]]}
const wordForms = {
  // Время
  age: ["год", "года", "лет"],
  month: ["месяц", "месяца", "месяцев"],
  day: ["день", "дня", "дней"],
  week: ["неделя", "недели", "недель"],
  hour: ["час", "часа", "часов"],
  minute: ["минута", "минуты", "минут"],
  second: ["секунда", "секунды", "секунд"],
  
  // Люди
  user: ["пользователь", "пользователя", "пользователей"],
  person: ["человек", "человека", "человек"],
  child: ["ребёнок", "ребёнка", "детей"],
  
  // Для нашего приложения
  year: ["год", "года", "лет"],      // дублируем для удобства
  years: ["год", "года", "лет"],     // альтернативное имя
  months: ["месяц", "месяца", "месяцев"],
  days: ["день", "дня", "дней"],
  weeks: ["неделя", "недели", "недель"]
};

// Удобная обертка для pluralForm
// Получает правильную форму слова по его имени из библиотеки wordForms
// @param {number} number - количество
// @param {string} formName - имя формы из wordForms
// @returns {string} - правильная форма
function getWordForm(number, formName) {
  // Получаем массив форм из библиотеки
  const forms = wordForms[formName];
  // Если формы нет в библиотеке, выводим предупреждение
  if (!forms) {
    console.warn(`⚠️ Нет формы для "${formName}", используйте pluralForm напрямую`);
    return pluralForm(number, ["", "", ""]);
  }
  // Используем универсальную функцию pluralForm
  return pluralForm(number, forms);
}

// Форматирует число со словом в правильной форме
// Пример: formatPlural(5, "year") → "5 лет" formatPlural(1, "month") → "1 месяц"
// @param {number} number - количество
// @param {string} formName - имя формы из wordForms
// @returns {string} - "5 лет", "1 год", "3 месяца"
function formatPlural(number, formName) {
  const word = getWordForm(number, formName);
  return `${number} ${word}`;
}

