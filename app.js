// Возвращает русское название месяца по его номеру
// Используется для понятных сообщений об ошибках
// @param {number} month - номер месяца (1-12)
// @returns {string} - название месяца в предложном падеже
function getMonthName(monthNumber) {
  // Массив названий месяцев в предложном падеже ("в январе", "в феврале" и т.д.)
  // Индекс 0 соответствует январю (месяц 1), поэтому используем monthNumber - 1
  const months = [
    "январе",     // 1
    "феврале",    // 2
    "марте",      // 3
    "апреле",     // 4
    "мае",        // 5
    "июне",       // 6
    "июле",       // 7
    "августе",    // 8
    "сентябре",   // 9
    "октябре",    // 10
    "ноябре",     // 11
    "декабре",    // 12
  ];
  // Возвращаем название месяца или "неизвестном месяце", если номер некоректен
  // month - 1 потому, что массивы в JavaScript начинаются с 0
  return months[monthNumber - 1] || "неизвестном месяце";
}

// Функция fixYearForUserList(year) исправляет двухзначный год на 4-х значный с учётом текущего времени
// Учитывает долгожителей старше 100 лет
// Логика: если год больше текущего остатка от 100 → 1900-е если меньше или равен → 2000-е
// Пример: текущий год 2026, остаток 26 год "27" → 27 > 26 → 1927 год "25" → 25 ≤ 26 → 2025
// @param {number} year - год (2 или 4 цифры)
// @returns {number} - исправленный на 4-х значный год
function fixYearForUserList(year) {
  // 1. Если год уже 4-х значный, возвращаем как есть
  if (year >= 1000) {   // 1000, а не 100, чтоб учесть годы с 999
    return year;
  }
  // 2. Получаем текущий год или последние его две цифры
  const currentYear = new Date().getFullYear(); // Например 2026
  const currentShortYear = currentYear % 100; // Например 26 (2026 % 100 = 26)

  // 3. ЛОГИКА ВЫБОРА ВЕКА:
  //    - Если двухзначный год БОЛЬШЕ текущих двух цифр → 1900-е
  //      Пример: год "27", текущий "26" → 27 > 26 → 1927 год
  //      (человек родился в прошлом веке, сейчас ему около 99 лет)

  //    - Если двухзначный год МЕНЬШЕ или РАВЕН текущим → 2000-е
  //      Пример: год "25", текущий "26" → 25 ≤ 26 → 2025 год
  //      (человек родился в этом веке, сейчас ему около 1 года)
  if (year > currentShortYear) {
    return year + 1900; // Для старшего поколения (1927-1999)
  } else {
    return year + 2000; // Для младшего поколения (2000-2026)
  }
}

// Умный парсинг даты из строки
// Понимает форматы: ДД-ММ-ГГГГ, ГГГГ-ММ-ДД, ДД.ММ.ГГГГ, ДД-ММ-ГГГГ
// Работает с любыми разделителями (- . / пробел и др.)
// Защищена от некорректного ввода (31 февраля, месяц 13 и т.д.)
// @param {string} dateStr - строка с датой
// @returns {Date|null} - объект Date и null при ошибке
function parseDate(dateStr) {
  // Если строка пустая или не передана, возвращаем null
  if (!dateStr) return null;

  // 1. ИЗВЛЕКАЕМ ВСЕ ЧИСЛА ИЗ СТРОКИ
  // Регулярное выражение /\d+/g находит все последовательности цифр в строке
  const numbers = dateStr.match(/\d+/g);
  // проверяем, что нашли минимум 3 числа (день, месяц, год)
  if (!numbers || numbers.length < 3) {
    console.error("❌ parseDate: Нужно 3 числа (день, месяц, год)");
    return null; // Не нашли 3 числа, тогда возвращаем null
  }

  // 2. ПРЕОБРАЗУЕМ ПЕРВЫЕ ТРИ ЧИСЛА В ЦЕЛЫЕ ЧИСЛА
  // parseInt(строка, 10) преобразует строку в число по основанию 10 (десятичная система)
  const num1 = parseInt(numbers[0], 10);    // Берём numbers[0] для решения исходя из того, сколько там цифр
  const num2 = parseInt(numbers[1], 10);    // Берём numbers[0] для решения исходя из того, сколько там цифр
  let year = parseInt(numbers[2], 10);      // Берём numbers[2] для решения исходя из того, сколько там цифр (предполагаемый год)

  // 3. ОПРЕДЕЛЯЕМ ФОРМАТ ДАТЫ
  let day, month;   // Переменные для хранения дня и месяца

  // ====== ДАЛЕЕ ТРИ ВАРИАНТА ДЕЙСТВИЙ С НАЙДЕННЫМ ГОДОВЫМ РЕЗУЛЬТАТОМ (с 4-мя цифрами) ======
  // Вариант А: Пример: "02-01-2027" Если третье число состоит из 4-х цифр -> ГГГГ в конце -> ДД-ММ-ГГГГ
  if (numbers[2].length === 4) {
    day = num1;                           // День в первом числе тут 2 цафры, значит не оно
    month = num2;                         // Месяц во втором числе тут тоже 2 цифры, значит тоже не оно
    // year уже установлен в функции, что если там 4 цифры, то это год, значит это оно
  }
  // Вариант В: Пример: "2027-01-02" Если первое число оказалось из четырёх цифр -> ГГГГ в конце -> ГГГГ-ММ-ДД
  else if (numbers[0].length === 4) {
    year = num1;                          // Если первое число с индексом[0] состоит из 4-х цифр, то это годовое число
    month = num2;                         // Тогда месяцем считается второе число если оно из 2-х цифр
    day = parseInt(numbers[2], 10);       // День тогда считается третьим числом в списке. Ожидается увидеть тоже 2 цифры
  }
  // Вариант С: Если формат не определён, , используем ДД-ММ-ГГГГ по умолчанию. Пример: "02-01-2027"
  else {
    day = num1;                           // Тогда первое число - это день
    month = num2;                         // Тогда второе число - это месяц
    // year уже утановлен как ГГГГ. Но может быть двухзначным, обработаем это позже
  }

  // ====== ВАЛИДНОСТЬ ДАТЫ ЕСЛИ ОНА ДВУХЗНАЧНАЯ ======
  // 4. Исправляем двухзначный год с учётом реальных дат рождения
  // Если год указан как "95", то нужно определить, это 1995 или 2095 ?
  // Функция fixYearForUserList() анализирует текущий год и определяет век
  year = fixYearForUserList(year);

  // ====== ПРОВЕРКА КОРРЕКТНОСТИ ДАТЫ ======
  // 5. Проверяем, что значение дня, месяцаи года реалистичны
  // Это защита от явно некорректных данных, введённых пользователем

  // 5.1 ОСНОВНАЯ ПРОВЕРКАДНЯ: проверяем диапазон дней в месяце (1-31)
  // Эта проверка отсекает самые грубые ошибки: день меньше 1 или больше 31
  if (day < 1 || day > 31) {
    console.error(`❌ parseDate: День ${day} вне диапазона от 1-го по 31-ое число в месяце`);
    return null;    // проверяем выполнение, возвращаем null
  }

  // 5.1.1 УТОЧНЁННАЯ ПРОВЕРКА ДЛЯ МЕСЯЦЕВ С 30-Ю ДНЯМИ:
  // Апрель(4), Июнь(6), Сентябрь(9), Ноябрь(11) имеют только 30 дней
  // Проверяем, не пытаются ли указать 31-е число в этих месяцах
  if (day === 31 && (month === 4 || month === 6 || month === 9 || month === 11)) {
    console.error(`❌ parseDate: В ${getMonthName(month)} только 30 дней`);
    return null;    // Прерываем выполнение
  }

  // 5.1.2 ОТДЕЛЬНАЯ ДОППРОВЕРКА ДЛЯ ФЕВРАЛЯ:
  // Применяем тернарный оператор для определения сколько в феврале дней? 28 или 29.
  if (month === 2) {
    // Проверяем, високосный ли год
    // Год високосный, если: делится на 400 === ИЛИ (делится на 4 И НЕ делится на 100)
    const isLeapYear = (year % 400 === 0) || (year % 4 === 0 && year % 100 !== 0);
    // Тернарный оператор работает как предложение:
    // Если год високосный, то 29 дней, иначе 28 дней
    const maxDaysInFebruary = isLeapYear ? 29 : 28;
    // Если дней указано больше то пишем сообщение об ошибке в консоле и возвращаем null
    if (day > maxDaysInFebruary) {
      // Выведет например: ❌ parseDate: В феврале 2025 года только 28 дней
      console.error(`❌ parseDate: В феврале ${year} года только ${maxDaysInFebruary} ${getWordForm(maxDaysInFebruary, "day")}`);
      return null;    // Прерываем выполнение
    }
  }

  // 5.2 ПРОВЕРКА МЕСЯЦА: Проверяем диапазон месяцев в годе (1-12)
  // Месяц не может быть меньше 1 (января) или больше 12 (декабря)
  if (month < 1 || month > 12) {
    console.error(`❌ parseDate: Месяц ${month} вне диапазона от 1-го по 12-ого месяца в году`);
    return null;    // Прерываем выполнение
  }
  // 5.3 ПРОВЕРКА ГОДОВЫХ ГРАНИЦ: Проверяем годовые границы (разумные границы летоисчислений на основе реалий)
  // Текущий год получаем из системной даты
  const currentYear = new Date().getFullYear();
  // Определяем возраст age из введённой даты year и вычитаем из неё текущую дату currentYear
  const age = currentYear - year;   // Предварительный расчет возраста (без учета месяца и дня)

  // 5.3.1 Проверка на слишком ранний год:
  // Устанавливаем рекордный возраст человека
  const maxHumanAge = 122;    // Рекордный возраст человека. Рекорд француженки Жанны Кальман - 122 года
  // Если возраст больше рекордного в 122 года, то выводим ошибку с сообщением вконсоли
  if (age > maxHumanAge) {
    console.warn(`⚠️ parseDate: Год ${year} слишком ранний. Если бы человек родился в этом году, ему было бы ${age} ${getWordForm(age, "year")}. Самый старый зарегистрированный возраст человека - ${maxHumanAge} ${getWordForm(maxHumanAge, "year")}. Проверьте дату рождения`);
    //return null;    // Прерываем выполнение для явно невозможных дат
    // 1900 - условная граница, раньше которой маловероятно, что кто-то из ныне живущих родился
  } else if (year < 1900) {
    console.warn(`⚠️ parseDate: Год ${year} очень ранний. Если человек и в правду родился в этом году, то ему сейчас ${age} ${getWordForm(age, "year")}. Если это так, то ОК! Если нет, то перепроверьте дату рождения`);
    // Здесь не возвращаем null, а только предупреждение в консоль
    // Возможно, это действительно старый человек или ошибка ввода
  }

  // 5.3.2 ПРОВЕРКА ОТРИЦАТЕЛЬНОГО ВОЗРАСТА:
  // Проверяем, не пытаются ли ввести дату рождения позже текущей даты
  // Человек не может родиться в будущем (+1 год на опечатки или округления)
  if (year >currentYear + 1) {
    console.error(`❌ parseDate: Год ${year} ещё не наступил`);
    return null;    // прерываем выполнение
  }

  // ====== СОЗДАЁМ ОБЪЕКТ ДАТЫ ======
  // 6. СОЗДАЁМОБЪЕКТ Date ИЗ ПРОВЕРЕННЫХ ЗНАЧЕНИЙ
  // ВАЖНО: в JavaScript месяц нумеруется с 0 (январь = 0, февраль = 1, ... декабрь = 11)
  // Поэтому передаём month - 1
  const date = new Date(year, month -1, day);

  // ====== ФИНАЛЬНАЯ ПРОВЕРКА ВАЛИДНОСТИ ======
  // 7. Проверяем, что дата валидна (не 31 февраля и т.д.)
  // Эта проверка нужна,потому что JavaScript автоматически корректирует несуществующие даты
  // Например: new Date(2024, 1, 31) (31 февраля) создаст дату 2 марта 2024. Если год не високосный то 31 февраля будет соответствовать 3 марта
  // В этом случае date.getDate() вернёт 2, а не 31
  if (date.getFullYear() !== year || date.getMonth() !== month - 1 || date.getDate() !== day) {
    console.error(`❌ parseData: Дата ${day}.${month}.${year} не существует`);
    return null;    // Прерываем выполнение. null не деёт выдать будущую дату если введена число днявфеврале больше чем нужно
  }
  // 8. Возвращаем успешно созданный объект Date
  return date;
}

// Форматирует дату в русский формат (15 марта 1995)
// Добавляет пометку "(в будущем)" для дат, которые еще не наступили
// @param {string} dataStr - строка с датой
// @returns {string} - отформатированная дата ("15 марта 1995 г." или "2 января 2027 г. (в будущем)")
function formatDate(dataStr){
  // 1. Парсим строку в объект Date
  const date = parseDate(dataStr);
  // 2. если парсинг не удался
  if (!date) return "не указана или некорректна";

  // 3. Проверяем, является ли дата исторической
  const currentYear = new Date().getFullYear();
  const isHistorical = date.getFullYear() < 1900;
  const isFuture = date > new Date();

  // 4. Настройки форматирования для русской локали
  const options ={
    day: 'numeric',   // Число (15)
    month: 'long',    // Полное название месяца (марта)
    year: 'numeric'   // Год (1995)
  };
  // 5. Форматируем дату спомощью встроенной функции
  const formattedDate = date.toLocaleDateString('ru-RU', options);
  // 6. Если дата в будущем, добавляем специальную пометку
  if (isFuture) {
    return `${formattedDate} (в будущем)`;
  } else if (isHistorical) {
    return `${formattedDate} (историческая дата)`;
  }
  // 7. Возвращаем отформатированную дату
  return formattedDate;
}

