/*
  Определяет, мог ли человек иметь email в год рождения
  Email появился в 1971 году, массовое распространение получил с 1990-х
  @param {number} birthYear - год рождения
  @returns {boolean} - true усли email мог существовать
*/
function couldHaveEmail(birthYear) {
  // Первый email был отправлен в 1971 году
  // Массовое распространение началось в 1990-х
  // Предположим что у человека мог появиться email в 20 лет
  return birthYear + 20 >= 1990;    // Родившийся в 1970 или позже
}

/*
  Определяет, мог ли человек иметь телефон в год своего рождения
  Телефоны появились в 1876-м году, но массовое распространение - с 1950-х
  @param {number} birthYear - год рождения
  @returns {boolean} - true если телефон мог существовать
*/
function couldHavePhone(birthYear) {
  // Телефон изобретён в 1876 году
  // Массовое распространение в городах - с 1910-х
  // В сельской местности - с 1950-х годов
  // предположим, что человек мог иметь телефон в 30 лет
  return birthYear + 30 >= 1910;    // Родившиеся в 1880 или позже
}

/*
  Генерирует случайное число заданной длины для телефонных номер
  @param {number} length - количество цифр
  @param {number} maxFirstDigit - максимальная первая цифра (по умолчанию это 9)
  @returns {string} - случайное число ввиде строки
*/
function generateRandomNumber(length, maxFirstDigit = 9) {
  let result = "";
  // Первая цифра (не может быть 0)
  result += Math.floor(Math.random() * maxFirstDigit) + 1;
  // Остальные цифры
  for (let i = 1; i < length; i++) {
    result += Math.floor(Math.random() * 10);
  }
  return result;
}

