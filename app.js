// ====== 0.4.2 ИСТОРИЯ: ПРОВЕРКА ТЕЛЕФОНА "МОГ ЛИ ОН СУЩЕСТВОВАТЬ" ======
// ============================================================================
// Идея: мы не пытаемся знать все коды всех городов,
// но можем по формату номера и году определить "реалистично/нереалистично".
// Это полезно, если пользователь вводит номер вручную.

// Упрощённая граница "мобильные массово реальны"
const MOBILE_REALISTIC_FROM_YEAR = 2005;

/* Приводим телефон к "чистому" виду: оставляем только цифры
   Пример: "+7 (925) 123-45-67" -> "79251234567"
*/
function normalizePhoneDigits(phone) {
  if (!phone) return "";
  return String(phone).replace(/\D/g, "");
}

/* 
  Проверяем вяглядит ли проверяемый номер как мобильный РФ (9XX...)
  Мы смотрим на нормализованные цыфры:
  - "7925..." (Начинается с 7, потом 9)
  - "8925..." (Начинается с 8, потом 9)
*/
function looksLikeMobileRU(digits) {
  // Проверяем длину номера телефона (должно быть 11 цифр в номере)
  if (digits.length < 11) return false;
  // В РФ часто аишкт номер с 8ХХХ... или 7ХХХ...
  if (digits.startsWith("7")) return digits[1] === "9";
  if (digits.startsWith("8")) return digits[1] === "9";
  return false;
}

/* 
  Проверяем: выглядит ли номер как "современный городской" РФ
  Типовые варианты:
  - +7 (495) ХХХ-ХХ-ХХ -> "7495XXXXXXX"
  - 8 (495) ХХХ-ХХ-ХХ -> "8495XXXXXXX"
  То есть: 11 цифр, начинаются на 7 или 8, далее 3 цифры кода и 7 цифр номера.
*/
function looksLikeModernGeoRU(digits) {
  if (digits.length !== 11) return false;
  if (!( digits.startsWith("7") || digits.startsWith("8"))) return false;
  // После 7/8 идёт код (3 цифры) и местный номер (7 цифр)
  const code = digits.slice(1, 4);
  const local = digits.slice(4);
  // Простая проверка, что это реально цифры нужно длины
  return code.length === 3 && local.length === 7;
}

/*
  Проверяем: выглядит ли номер как "старый городской" (СССР/ранняя РФ)
  Это могут быть короткие номера 5-7 цифр, часто с дефисами:
  "2-12-34", "12-34-56", "123-15", "1234567" и т.п.
  Мы делаем упрощённую эвристику:
  - если в нормализованном виде 5..7 цифр -> считаем старым локальным
*/
function looksLikeOldLocal(digits) {
  return digits.length >= 5 && digits.length <= 7;
}

/*
  Главная функция проверки:
  Возвращает объект { ок: boolean, reason: string }
  referenceYear - год "активности" (lastActiveYear): год смерти или текущий год
  city - город (пока используется слабо, в будущем расширится)
*/
function isPhonePlausible(phone, referenceYear, city) {
  // 1. Базовая проверка
  if (!phone) {
    return { ok: true, reason: "Телефон не указан"};
  }
  // 2. Если по времени телефона ещё не могло быть
  if (referenceYear < 1876) {
    return { ок: false, reason: "Телефон ещё не был изобретён (до 1876 г)"};
  }
  // 3. Нормализуем в цифры
  const digits = normalizePhoneDigits(phone);
  // Если слишком мало цифр - явно странно
  if (digits.length < 5) {
    return { ок: false, reason: "Слишком короткий номер" };
  }
  // 4. Мобильный
  if (looksLikeMobileRU(digits)) {
    if (referenceYear < MOBILE_REALISTIC_FROM_YEAR) {
      return {
        ок: false, reason: `Мобильные номера в таком виде малореалистичны раньше ${MOBILE_REALISTIC_FROM_YEAR} года`
      };
    }
    return {
      ок: true, reason: "Похоже на мобильный номер (реалистично для современных лет)"
    };
  }
  // 5. Современный городской (+7/8 + АВС +7 цифр)
  if (looksLikeModernGeoRU(digits)) {
    // Упрощённо: такой формат формат точно не раньше 1990-х
    if (referenceYear < 1993) {
      return { ок: false, reason: "Современный формат +7/8(АВС)ХХХ-ХХ-ХХ малореалистичен до 1993 года"};
    }
    return { ок: true, reason: "Похоже на современный городской номер" };
  }
  // 6. Старый локальный (5-7 цифр)
  if (looksLikeOldLocal(digits)) {
    // Старые короткие номера логичнее до 1993
    if (referenceYear >= 2000) {
      return { ок: false, reason: "Короткий городской номер малореалистичен для 2000+ (обычно уже +7 и код)" };
    }
    return { ок: true, reason: "Похоже на старый городской/локальный номер" };
  }
  // 7. Всё остальное - "не знаем", но предупреждаем
  return {
    ок: true, reason: "Формат номера нестандартный: точную проверку сделать сложно (пока нет справочника городов)"
  };
}


/*
  Генерирует исторически корректный телефонный номер для России/СССР
  на основе последнего активного года (смерть или текущий) и города.
  Учитывает изменения номерной ёмкости и форматов по периодам.
  @param {number} activeYear   - год, на который генерируется номер (год смерти или текущий)
  @param {string} city         - город проживания
  @returns {string}            - строка с исторически корректным номером
*/
function getHistoricalPhoneNumber(activeYear, city) {
  // 1. Если активный год раньше изобретения телефона (1876) - возвращаем текст
  if (activeYear < 1876) {
    return "Телефон ещё не был изобретён";
  }
  // 2. Если город не указан - возвращаем универсальный номер эпохи
  if (!city) {
    return generateHistoricalNumberForYear(activeYear);
  }
  // Нормальзует название города для поиска
  const cityLower = city.toLowerCase();

  // 3. Специфичные форматы для Москвы (можно расширять на другие города)
  if (cityLower === "Москва") {
    return getMoscowPhoneNumber(activeYear);
  }
  // 4. Специфичные форматы для Санкт-Петербурга / Ленинграда
  if (cityLower === "Санкт-Петербург" || cityLower === "Ленинград") {
    return getSpbPhoneNumber(activeYear);
  }
  // 5. Для других городов - общий исторический формат
  return generateHistoricalNumberForYear(activeYear);
}

/*
  Вспомогательная функция: возвращает московский номер в зависимости от года.
  Использует данные из cityCodes["Москва"].
*/
function getMoscowPhoneNumber(year) {
  // Правила для москвы (взяты из справочно-исторических материалов)
  if (year < 1930) {
    // До 1930-го номера однозначные или двухзначные (например, "2-15")
    const prefix = Math.floor(Math.random() * 5) + 1;   // 1-5
    const suffix = Math.floor(Math.random() * 90) + 10; // 10-99
    return `${prefix}- ${suffix}`;
  }
  else if (year < 1940) {
    // C 1930-1940 года номера телефонов буквенно-цифровые (К-0-12)
    const letters = ["А", "Б", "В", "Г", "Д", "Е", "Ж", "И", "К", "Л", "М", "Н"];
    const letter = letters[Math.floor(Math.random() * letters.length)];
    const num1 = Math.floor(Math.random() * 5);
    const num2 = Math.floor(Math.random() * 90) + 10;
    return `${letter}-${num1}-${num2}`;
  }
  else if (year < 1960) {
    // C 1940-1960 года номера уже 4-5-ти значные без кода
    const length = year < 1950 ? 4 : 5;
    let number = "";
    for (let i = 0; i < length; i++) {
      number += Math.floor(Math.random() * 10);
    }
    return number;
  }
  else if (year < 1970) {
    // С 1960-1970 годов 5-ти значные с буквой (Г-12345)
    const letters = ["А", "Б", "В", "Г", "Д", "Е", "Ж", "И", "К"];
    const letter = letters[Math.floor(Math.random() * letters.length)];
    let number = "";
    for (let i = 0; i < 5; i++) {
      number += Math.floor(Math.random() * 10);
    }
    return `${letter}-${number}`;
  }
  else if (year < 1993) {
    // С 1970-1992 номера 6-значные (2-12-34) или 7-значные к концу периода
    const length = year < 1980 ? 6 : 7;
    let number ="";
    for (let i = 0; i < length; i++) {
      number += Math.floor(Math.random() * 10);
    }
    // Вставляем дефис для читаемости (как было принято)
    if (length === 6) {
      return `${number.slice(0,1)}-${number.slice(1,3)}-${number.slice(3)}`;
    } else {
      return `${number.slice(0,3)}-${number.slice(3,5)}-${number.slice(5)}`;
    }
  }
  else if (year < 2003) {
    // Уже с 1993-2002 года появился код 095 + 7 цифр номера телефона
    const local = generateRandomNumber(7);
    return `(095) ${local.slice(0,3)}-${local.slice(3,5)}-${local.slice(5)}`;
  }
  else if (year < 2007) {
    // С 2003-2006 года переходный период с кодов 095 на 495, так как телефония начала не справляться
    const code = Math.random() > 0.5 ? "095" : "495";
    const local = generateRandomNumber(7);
    return `(${code}) ${local.slice(0,3)}-${local.slice(3,5)}-${local.slice(5)}`;
  }
  else if (year < 2011) {
    // С 2007-2010 года уже два кода. Отпал 095, остался 495 и к нему добавили 499
    const code = Math.random() > 0.5 ? "495" : "499";
    const local = generateRandomNumber(7);
    return `(${code}) ${local.slice(0,3)}-${local.slice(3,5)}-${local.slice(5)}`;
  }
  else {
    // с 2011 используют уже три кода 495, 498, 499 и +7
    const codes = ["495", "498", "499"];
    const code = codes[Math.floor(Math.random() * codes.length)];
    const local = generateRandomNumber(7);
    return `+7 (${code}) ${local.slice(0,3)}-${local.slice(3,5)}-${local.slice(5)}`;
  }
}

/*
  Вспомогательная функция: возвращает петербургский/ленинградский номер в зависимости от года.
  Историческая вставка:
  - В Ленинграде, как и в Москве, использовались буквенные префиксы АТС.
  - На 7-ми-значную нумерацию город перешёл в 1972 году.
  - С 1993 года код города стал - 812.
*/
function getSpbPhoneNumber(year) {
  // === РАННИЙ ПЕРИОД (до 1968): буквенно-цифровые номера ===
  if (year < 1968) {
    const letters = ["А", "Б", "В", "Г", "Д", "Е", "Ж", "И", "К", "Л"];
    const letter1 = letters[Math.floor(Math.random() * letters.length)];
    // В 50-60-е годы могли быть двухбуквенные префиксы (например, АБ-1-23-45)
    if (year > 1950 && Math.random() > 0.5) {
      const letter2 = letters[Math.floor(Math.random() * letters.length)];
      const numPart = generateRandomNumber(year < 1960 ? 4 : 5);
      // Формат с двумя буквами: АБ-1-23-45
      return `${letter1}${letter2}-${numPart.slice(0,1)}-${numPart.slice(1,3)}-${numPart.slice(3)}`;
    }
  }
  // === ПЕРЕХОДНЫЙ ПЕРИОД (1968-1971): 6-ти или 7-ми-значные номера ===
}

/*
  Генерирует обобщённый исторический номер для любого города (когда нет специальных данных).
*/
function generateHistoricalNumberForYear(year) {
  if (year < 1930) {
    return `Местный ${Math.floor(Math.random() * 900) + 100}`;
  } else if (year < 1960) {
    return `${Math.floor(Math.random() * 90) + 10}-${Math.floor(Math.random() * 90) + 10}`;
  } else if (year < 1993) {
    return `${Math.floor(Math.random() * 900) + 100}-${Math.floor(Math.random() * 90) + 10}-${Math.floor(Math.random() * 90) + 10}`;
  } else {
    // современный формат с кодом города (условный)
    const cityCode = Math.floor(Math.random() * 900) + 100;
    const local = generateRandomNumber(7);
    return `(${cityCode}) ${local.slice(0,3)}-${local.slice(3,5)}-${local.slice(5)}`;
  }
}


function cleanHistoricalData(user) {
  // 1. Парсим дату рождения
  const birthDate = parseDate(user.birthDate);
  if (!birthDate) return user;   // Если дата рождения битая - возвращаем как есть
  // 2. Копируем пользователя чтобы не менять исходный userData
  const cleanedUser = { ...user };
  // 3. парсим дату смерти ОДИН раз
  const deathDate = parseDate(user.deathDate);
  // 4. определяем: жив или умер
  const isAlive = !deathDate;
  cleanedUser.lifeStatusText = isAlive ? "Жив" : `Умер: ${formatDate(user.deathDate)}`;
  // 5. год рождения и исторический период
  const birthYear = birthDate.getFullYear();
  cleanedUser.historicalPeriod = getHistoricalPeriod(birthYear);
  // 6. До какого года человек "активен"
  //    Если умер - до года смерти, если жив - до текущего года
  const lastActiveYear = deathDate ? deathDate.getFullYear() : new Date().getFullYear();

  // ====== ОБРАБОТКА EMAIL ======
  // Первый email отправлен в 1971 году Рэем Томлинсоном
  // Массовое распространение интернета в России началось с середины 1990-х
  // 7. email: мог ли иметь?
  if (lastActiveYear < 1971) {
    cleanedUser.email = "Электронная почта не была изобретена";
  } else if (lastActiveYear < 1995) {
    cleanedUser.email = "Ранняя электронная почта (до массового распространения)";
  } else if (lastActiveYear < 2005) {
    // 1995-2005: email уже есть, но не у всех; если в данных нет реального email, генерируем условный
    if (!user.email || user.email.includes("exemple") || user.email === "") {
      cleanedUser.email = `user${birthYear}@mail.ru`;   // Условный адрес того времени
    }
  }
  // Для современных лет оставляем исходный email (если он указан) или генерируем placeholder


  // ====== ОБРАБОТКА ТЕЛЕФОНА ======
  // 8. Телефон: мог ли иметь?
  if (lastActiveYear < 1876) {
    // Телефон изобретён Александром Беллом в 1976 году
    cleanedUser.phone = "Телефон ещё не был изобретён";  
  } else if (lastActiveYear < 1910) {
    // Ранняя телефония: очень редкие номера, часто однозначные
    cleanedUser.phone = `Ранний телефонный номер (пример: ${Math.floor(Math.random() * 900) + 100}`;
  } else if (lastActiveYear < 1993) {
    // Для исторических личностей (умерших до 1993 года) - генерируем номер по эпохе
    // ВАЖНО: передаём lastActiveYear, а не birthYear
    cleanedUser.phone = getHistoricalPhoneNumber(lastActiveYear, user.city);
  } else {
    // Современные люди (активны после 1993) - оставляем телефон из данных или генерируем современный
    if (!user.phone || user.phone === "") {
      cleanedUser.phone = getHistoricalPhoneNumber(lastActiveYear, user.city);
    } else {
      cleanedUser.phone = user.phone;
    }
  }

  // 9. ОБРАБОТКА ПРОФЕССИИ (простая историческая замена)
  if (birthYear < 1900) {
    const map = {
      "Разработчик": birthYear < 1800 ? "Учёный" : "Инженер",
      "Дизайнер": birthYear < 1800 ? "Художник" : "Архитектор",
      "Маркетолог": birthYear < 1800 ? "Торговец" : "Коммерсант",
      "Аналитик": birthYear < 1800 ? "Философ" : "Учёный"
    };
    if (map[cleanedUser.profession]) {
      cleanedUser.profession = map[cleanedUser.profession];
    }
  }
  // 10. TODO: возврат на момент смерти
  // Здесь можно добавить функции, которая считает детальный возраст между birthDate и deathDate
  return cleanedUser;
}


