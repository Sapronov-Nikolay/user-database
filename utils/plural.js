/**
  Универсальная функция склонения сообщений по падежам.
  @param {number} nember - Количество (нипример, 5).
  @param {Array} forms   - Массив из трёх форм [форма №1 для 1, форма №2 для 2-4, форма №3 для 5-20]
  @returns {string}      - Правильная форма слова по падежам (Например: 1 год, 2 года, 10 лет, 23 года)
*/
function pluralForm(number, forms) {
  // ШАГ 1: Проверяем, что forms - это массив ровно из трёх форм (трёх вариантов падежного произношения)
  if (!Array.isArray(forms) || forms.length !== 3){
    console.error('❌ pluralForm: нужен массив из 3-х форм');
    // На всякий случай возвращаем третью форму или пустоту для целостности.
    return forms[2] || '';
  }

  // ШАГ 2: Берём абсолютное значение числа (на случай отрицательных значений) и превращаем в число.
  const num = Math.abs(Number(number));

  // ШАГ 3: Вычисляем последние две цифры и последнюю цифру числа. Сформировали две переменные, с которыми и будем тут работать
  // Пример: 123 % 100 = 23 (последние две цифры)
  const lastTwo = num % 100;
  // Пример: 123 % 10 = 3 (последняя цифра)
  const lastOne = num % 10;

  // ШАГ 4: Исключения для чисел 11-14. Они ВСЕГДА требуют формы №3 (лет, дней, месяцев, недель).
  // Пример: 11 лет, 12 лет, 13 лет, 14 лет.
  if (lastTwo >= 11 && lastTwo <= 14) {
    return forms[2];
  }

  // ШАГ 5: Если число оканчивается на 1 (но не 11) → форма №1 (год, день, месяц, неделя).
  // Пример: 1 год, 21 год, 101 год.
  if (lastOne === 1) {
    return forms[0];
  }

  // ШАГ 6: Если число оканчивается на 2, 3, 4 (но не 12, 13, 14) → форма №2 (года, дня, месяца, недели).
  // Пример: 2 года, 3 года, 4 года, 22 года.
  if (lastOne >= 2 && lastOne <= 4) {
    return forms[1];
  }

  // ШАГ 7: Во всех остальных случаях (когда число не принято было в шагах 5 и 6 - выполняем шаг 4) → форма №3 (лет, дней, месяцев, недель).
  // Пример: 5 лет, 10 лет, 20 лет.
  return forms[2];
}

/**
  Удобная обёртка: получает правильную форму слова по его имени из библиотеки.

  @param {number} number    - Количество.
  @param {string} formName  - Имя формы из wordForms (например, "year").
  @returns {string}         - Правильная форма слова.
*/
function getWordForm(number, formName) {
  // ШАГ 1: Ищем массив форм по имени.
  const form = wordForms[formName];

  // ШАГ 2: Если формы нет в библиотеке - выводим предупреждение и возвращаем пустую строку.
  if (!forms) {
    console.warm(`⚠️ Нет формы для "${formName}`);
    return pluralForm(number, ["", "", ""]);
  }
  
  // ШАГ 3: Вызываем универсальную pluralForm с найденным массивом форм
  return pluralForm(number, forms);
}

/**
  Форматирует число со словом в правильной форме
  Пример: formatPlural(5, "year") → "5 лет".
  По сути складываем отображения числа с умением выбирать форму слова в виде единой готовой фразы "5 лет" или "22 года".

  @param {number} number    - Количество
  @param {string} formName  - Имя формы.
  @returns {string}         - "5 оет", "1 год", "3 месяца".
*/
function formatPlural(number, formName) {
  // Получаем правильную форму слова.
  const word = getWordForm(number, formName);
  // возвращаем строку вида "число слово".
  return `${number} ${word}`;
}

