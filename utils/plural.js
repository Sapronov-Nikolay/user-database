/**
  Склоняет полное имя (Фамилия Имя Отчество) или его часть в указанный падеж.
  Учитывает:
  - несклоняемые фамилии (на -ко, -енко, -ых, -их, -аго, -яго, -ово, -ево),
  - женские фамилии адъективного типа (на -ая/-яя, -ова/-ева/-ина/-ына),
  - женские фамилии существительного типа (прочие на -а/-я),
  - мужские фамилии адъективного типа (на -ый/-ий/-ой),
  - мужские фамилии на согласный (кроме выше),
  - имена 3-го склонения (Любовь, Надежда и т.п.) и имена на -ия (Мария).

  @param {object} user             - Объект пользователя (surname, name, patronymic, gender)
  @param {string} gramaticalCase   - Падеж: 'nominative', 'genitive', 'dative', 'accusative', 'instrumental', 'prepositional'.
  @param {object} [options]        - Дополнительные настройки:
  @param {string} optoons.part     - какая часть нужна: 'full' (по умолчанию), 'surname', 'name', 'patronymic'.
  @param {string} options.order    - Порядок слов: 'FIO' (Фамилия Имя Отчество, по умолчанию) или 'IOF' (Имя Отчество Фамилия).
  @returns {string}                - Склонённая часть или полное ФИО.
*/
function declineFullName(user, gramaticalCase, options = {}) {
  const {part = 'full', order = 'FIO'} = options;
  const {surname, name, patronymic, gender} = user;

  // ---------- ПРАВИЛА СКЛОНЕНИЯ СКЛОНЯЕМЫХ И НЕСКЛОНЯЕМЫХ КОМПОНЕНТОВ ----------
  /**
    Классические несклоняемые фамилии (для мужского рода тоже не склоняются).
    Женские фамилии на эти окончания и так не изменятся из-за отсутствия правил,
    но для единообразия оставляем.
  */
  function isIndeclinableSurname(s) {
    return /(ко|енко|ых|их|аго|яго|ово|ево|уко|юк|ук|ин|ын|ени|нии|еви)$/i.test(s);
  }

  // ---------- ВНУТРЕННЯЯ ФУНКЦИЯ СКЛОНЕНИЯ КОМПОНЕНТОВ ----------
  const declineParts = (gCase) => {
    // Инициализируем переменные для результатов
    let sur, nm, pat;

    // ---------- ЖЕНСКИЙ РОД ----------
    if (gender === 'female') {
      //                               --- Фамилия (женская) ---
      if (isIndeclinableSurname(surname)) {
        sur = surname; // Несклоняемая фамилия
      } else {
        // Определяем тип склонения фамилии
        const isAdjectiveFeminine = /(ая|яя|ова|ева|ина|ына)$/i.test(surname);
        if (isAdjectiveFeminine) {
          // Женские фамилии фдьективного типа (на -ая/-яя, -ова/-ева/-ина/-ына)
          switch (gCase) {
            case 'genitive':  // Родительный падеж (кого? чего?)
              sur = surname
                .replace(/ая$/, 'ой')
                .replace(/яя$/, 'ей')
                .replace(/ова$/, 'овой')
                .replace(/ева$/, 'евой')
                .replace(/ина$/, 'иной')
                .replace(/ына$/, 'ыной');
              break;
            case 'dative':    // Дательный падеж (кому? чему?)
              // Форма совпадает с родительным
              sur = surname
                .replace(/ая$/, 'ой')
                .replace(/яя$/, 'ей')
                .replace(/ова$/, 'овой')
                .replace(/ева$/, 'евой')
                .replace(/ина$/, 'иной')
                .replace(/ына$/, 'ыной');
              break;
            case 'accusative':    // Винительный падеж (кого? чего?)
              sur = surname
                .replace(/ая$/, 'ую')
                .replace(/яя$/, 'юю')
                .replace(/ова$/, 'ову')
                .replace(/ева$/, 'еву')
                .replace(/ина$/, 'ину')
                .replace(/ына$/, 'ыну');
              break;
            case 'instrumental':   // Творительный падеж (кем? чем?)
              sur = surname
                .replace(/ая$/, 'ой')
                .replace(/яя$/, 'ей')
                .replace(/ова$/, 'овой')
                .replace(/ева$/, 'евой')
                .replace(/ина$/, 'иной')
                .replace(/ына$/, 'ыной');
              break;
            case 'prepositional':   // Предложный падеж (о ком? о чём? где? когда?)
              sur = surname
                .replace(/ая$/, 'ой')
                .replace(/яя$/, 'ей')
                .replace(/ова$/, 'овой')
                .replace(/ева$/, 'евой')
                .replace(/ина$/, 'иной')
                .replace(/ына$/, 'ыной');
              break
            case 'nominative':    // Именительный падеж (кто? что?)
            default:
              sur =surname;   // Именительный падеж не нуждается в переделке окончаний - replace() не требуется
          }
        } else {
          // Женские фамилии существительного типо (прочие на -а/-я, включая типа "Пелагея")
          switch (gCase) {
            case 'genitive':        // Родительный падеж (кого? чего?)
              sur = surname
                .replace(/а$/, 'ы')
                .replace(/я$/, 'и');
              break;
            case 'dative':          // Дательный падеж (кому? чему?)
              sur = surname
                .replace(/а$/, 'е')
                .replace(/я$/, 'е');
              break;
            case 'accusative':      // Винительный падеж (кого? что?)
              sur = surname
                .replace(/а$/, 'у')
                .replace(/я$/, 'ю');
              break;
            case 'instrumental':    // Творительный падеж (кем? чем?)
              sur = surname
                .replace(/а$/, 'ой')
                .replace(/я$/, 'ей');
              break;
            case 'prepositional':   // Предложный падеж (о ком? о чём? где? когда?)
              sur = surname
                .replace(/а$/, 'е')
                .replace(/я$/, 'е');
              break;
            case 'nominative':      // Именительный падеж (кто? что?)
            default:
              sur = surname;        // Именительный падеж не нуждается в переделке окончаний - replace() не требуется
          }
        }
      }

      //                               --- Имя (женское) ---
      // Обрабатываем: 1-е склонение на -а/я, особые на -ия, 3-е склонение на -ь (Любовь и т.п.)
      const isThirdDeclension = name.endsWith('ь') && !name.endsWith('а') && !name.endsWith('я');
      if (isThirdDeclension) {
        switch (gCase) {
          case 'genitive':      // Родительный падеж (кого? чего?)
          case 'dative':        // Дательный падеж (кому? чему?)
          case 'prepositional': // Предложный падеж (о ком? о чём? где? когда?)
            nm = name.slice(0, -1) + 'и'; // Любовь → Любови, Надежда → Надежды (не 3-е слонение не попадёт)
            break;
          case 'accusative':    // Винительный падеж (кого? что?)
            nm = name;          // Любовь → Любовь (не склоняется)
            break;
          case 'instrumental':  // Творительный падеж (кем? чем?)
            nm = name.slice(0, -1) + 'ью'; // Любовью
            break;
          case 'nominative':    // Именительный падеж (кто? что?)
          default:
            nm = name;          // Именительный падеж не нуждается в переделке окончаний - replace() не требуется
        }
      } else {
        // Обычные на -а/-я включая -ия
        switch (gCase) {
          case 'genitive':      // Родительный падеж (кого? чего?)
            nm = name
              .replace(/ия$/, 'ии') // "Мария" → "Марии"
              .replace(/я$/, 'и')   // "Пелагея" → "Пелагеи"
              .replace(/а$/, 'ы');  // "Анна" → "Анны"
            break;
          case 'dative':        // Дательный падеж (кому? чему?)
            nm = name
              .replace(/ия$/, 'ии') // "Мария" → "Марии"
              .replace(/я$/, 'и')   // "Пелагея" → "Пелагеи"
              .replace(/а$/, 'е');  // "Анна" → "Анне"
            break;
          case 'accusative':    // Винительный падеж (кому? чему?)
            nm = name
              .replace(/ия$/, 'ию')  // "Мария" → "Марию"
              .replace(/я$/, 'ю')    // "Пелагея" → "Пелагею"
              .replace(/а$/, 'у');   // "Анна" → "Анну"
            break;
          case 'instrumental':   // Творительный падеж (кому? чему?)
            nm = name
              .replace(/ия$/, 'ией')  // "Мария" → "Марией"
              .replace(/я$/, 'ей')    // "Пелагея" → "Пелагеей"
              .replace(/а$/, 'ой');   // "Анна" → "Анной"
            break;
          case 'prepositional':  // Предложный падеж (о ком? о чём? где? когда?)
            nm = name
              .replace(/ия$/, 'ии')   // "Мария" → "Марии"
              .replace(/я$/, 'е')     // "Пелагея" → "Пелагее"
              .replace(/а$/, 'е');    // "Анна" → "Анне"
            break;
          case 'nominative':  // Именительный падеж (кто? что?)
          default:
            nm = name;        // Именительный падеж не нуждается в переделке окончаний - replace() не требуется
        }
      }

      //                               --- Отчество (женское) ---
      switch (gCase) {
        case 'genitive':      // Родительный падеж (кого? чего?)
          pat = patronymic.replace(/на$/, 'ны');  // "Владимировна" → "Владимировны"
          break;
        case 'dative':        // Дательный падеж (кому? чему?) - Окончания совпадают с предложным "prepositional"
        case 'prepositional': // Предложный падеж (о ком? о чём? где? когда?)
          pat = patronymic.replace(/на$/, 'не');  // "Владимировна" → "Владимировне"
          break;
        case 'accusative':    // Винительный падеж (кого? что?)
          pat = patronymic.replace(/на$/, 'ну');  // "Владимировна" → "Владимировну"
          break;
        case 'instrumental':  // Творительный падеж (кем? чем?)
          pat = patronymic.replace(/на$/, 'ной'); // "Владимировна" → "Владимировной"
          break;
        case 'nominative':    // Именительный падеж (кто? что?)
        defoult:
          pat = patronymic;   // Именительный падеж не нуждается в переделке окончаний - replace() не требуется
      }
    }

    // ========== МУЖСКОЙ РОД ==========
    else {
      //                               --- Фамилия (мужская) ---
      if (isIndeclinableSurname(surname)) {
        sur = surname;    // Не склоняются
      } else {
        // Проверяем, не адъективная ли мужская фамилия (на -ый/-ий/ой)
        const isAdjectiveMasculine = /(ый|ий|ой|)$/i.test(surname);
        if (isAdjectiveMasculine) {
          // Мужские фамилии фдъективного типа (Белый, Толстой, Благой)
          switch (gCase) {
            case 'genitive':      // Родительный падеж (кого? чего?)
              sur = surname
                .replace(/ый$/, 'ого')
                .replace(/ий$/, 'его')
                .replace(/ой$/, 'ого');
              break;
            case 'dative':        // Дательный падеж (кому? чему?)
              sur = surname
                .replace(/ый$/, 'ому')
                .replace(/ий$/, 'ему')
                .replace(/ой$/, 'ому');
              break;
            case 'accusative':    // Винительный падеж (кого? что?)
              // Для одушевлённых совпадает с родительным
              return declineParts('genitive');
            case 'instrumental':  // Творительный падеж (кем? чем?)
              sur = surname
                .replace(/ый$/, 'ым')
                .replace(/ий$/, 'им')
                .replace(/ой$/, 'ым');
              break;
            case 'prepositional': // Предложный падеж (о ком? о чём? где? когда?)
              sur = surname
                .replace(/ый$/, 'ом')
                .replace(/ий$/, 'ем')
                .replace(/ой$/, 'ом');
              break;
            case 'nominative':    // Именительный падеж (кто? что?)
            default:
              sur = surname;      // Именительный падеж не нуждается в переделке окончаний - replace() не требуется
          }
        } else {
          // Мужские фамилии не согласный (субстантивного типа) - Попов, Аракчеев и т.п.
          switch (gCase) {
            case 'genitive':        // Родительный падеж (кого? чего?)
              sur = surname + 'а';
              break;
            case 'dative':          // Дательный падеж (кому? чему?)
              sur = surname + 'у';
              break;
            case 'accusative':      // Винительный падеж (кого? что?)
              sur = surname + 'а';  // как родительный
              break;
            case 'instrumental':    // Творительный (кем? чем?)
              sur = surname + 'ым';
              break;
            case 'prepositional':   // Предложный падеж (о ком? о чём? где? когда?)
              sur = surname + 'е';
              break;
            case 'nominative':      // Именительный падеж (кто? что?)
            default:
              sur = surname;        // Именительный падеж не нуждается в переделке окончаний - replace() не требуется
          }
        }
      }

      //                               --- Имя (мужское) ---
      switch (gCase) {
        case 'genitive':                    // Родительный падеж (кого? чего?)
          nm = name
            .replace(/н$/, 'на')    // "Иван" → "Ивана"
            .replace(/р$/, 'ра')    // "Пётр" → "Петра"
            .replace(/й$/, 'я');    // "Алексей" → "Алексея"
          break;
        case 'dative':                      // Дательный (кому? чему?)
          nm = name
            .replace(/н$/, 'на')    // "Иван" → "Ивана"
            .replace(/р$/, 'ру')    // "Пётр" → "Петру"
            .replace(/й$/, 'ю');    // "Алексей" → "Алексею"
          break;
        case 'accusative':                  // Винительный (кого? что?)
          return declineParts('genitive');  // Для одушевлённых совпадает с родительным
        case 'instrumental':                // Творительный (кем? чем?)
          nm = name
            .replace(/н$/, 'ном')    // "Иван" → "Иваном"
            .replace(/р$/, 'ром')    // "Пётр" → "Петром"
            .replace(/й$/, 'ем');    // "Алексей" → "Алексем"
          break;
        case 'prepositional':               // Предложный падеж (о ком? о чём? где? когда?)
          nm = name
            .replace(/н$/, 'не')    // "Иван" → "Иване"
            .replace(/р$/, 'ре')    // "Пётр" → "Петре"
            .replace(/й$/, 'е');    // "Алексей" → "Алексее"
          break;
        case 'nominative':                  // Именительный падеж (кто? что?)
        default:
          nm = name;                // Именительный падеж не нуждается в переделке окончаний - replace() не требуется
      }

      //                               --- Отчество (мужское) ---
      switch (gCase) {
        case 'genitive':            // Родительный (кого? чего?)
          pat = patronymic + 'а';   // "Фёдорович" → "Фёдоровича"
          break;
        case 'dative':              // Дательный (кому? чему?)
          pat = patronymic + 'у';   // "Фёдорович" → "Фёдоровичу"
          break;
        case 'accusative':          // Винительный (кого? что?) совпадает с родительным
          pat = patronymic + 'а';   // "Фёдорович" → "Фёдоровича"
          break;
        case 'instrumental':        // Творительный (кем? чем?)
          pat = patronymic + 'ем';  // "Фёдорович" → "Фёдоровичем"
          break;
        case 'prepositional':       // Творительный (кем? чем?)
          pat = patronymic + 'е';   // "Фёдорович" → "Фёдоровиче"
          break;
        case 'nominative':          // Именительный падеж (кто? что?)
        default:
          pat = patronymic;         // Именительный падеж не нуждается в переделке окончаний - replace() не требуется
      }
    }
    // Возвращаем объект со склонёнными частями
    return {surname: sur, name: nm, patronymic: pat};
  };

  // Получаем объект со склонёнными компонентами
  const declined = declineParts(gramaticalCase);

  // Если запрошено конкретная часть - возвращаем только её
  if (part === 'surname')
    return declined.surname;
  
  if (part === 'name')
    return declined.name;

  if (part === 'patronymic')
    return declined.patronymic;

  // Сборка строки в указанном порядке
  if (order === 'IOF') {
    return `${declined.name} ${declined.patronymic} ${declined.surname}`;
  }
  // Иначе по умолчанию - ФИО
  return `${declined.surname} ${declined.name} ${declined.patronymic}`;
}