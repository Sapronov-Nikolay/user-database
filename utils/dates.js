/**
  Форматирует дату в читаемый русский формат: "15 марта 1995г."
  Добавляет пометки "(в будущем)" или "(исторические даты)".

  @param {string} dataStr - Строка с датой.
  @returns {string} - Отформатированная строка.
*/
function formatDate(dataStr) {
  // Парсим строку в объект Date/
  const date = parseDate(dataStr);
  // Если парсинг не удался, возвращаем сообщение об ошибке.
  if (!date) {
    return "не указана или некоректна";
  }

  // Определяем, является ли дата исторической или датой из будущего
  const isHistorical = isHistoricalYear(date.getFullYear());
  const isFuture = date > new Date();
  // Настройки для русской локали: число, полное название месяца, год.
  const options = {day: 'numeric', month: 'long', year: 'numeric'};
  const formatted = date.toLocaleDateString('ru-RU', options);
  // Добавляем соответствующие пометки в зависимости от даты.
  if (isFuture) {
    return `${formatted} (в будущем)`;
  } else if (isHistorical) {
    return `${formatted} (историческая дата)`;
  }else {
    return formatted;
  }
}

/**
  Вычисляет возраст в годах (целое число).
  Если дата вбудущем - возвращает 0.

  @param {string} birthDateStr - Дата рождения.
  @return {number} - Возраст в годах.
*/
function calculateAge(birthDateStr) {
  // Получаем дату рождения из строки
  const birthDate = parseDate(birthDateStr);
  // Если дата не прошла валидацию, возвращаем 0
  if (!birthDate) return 0;

  // Получаем текущую дату
  const today = new Date();
  // Если дата рождения в будущем, возвращаем 0 (возраст не может быть отрицательным)
  if (birthDate > today) {
    // Выводим предупреждение в консоль
    console.warn(`⚠️ calculateAge: Дата рождения ${birthDateStr} в будущем`);
    return 0;
  }

  // Вычисляем предварительный возраст
  let age = today.getFullYear() - birthDate.getFullYear();
  // Проверяем, прошёл ли уже день рождения в этом году
  const hasPassed = today.getMonth() > birthDate.getMonth() ||
    (today.getMonth() === birthDate.getMonth() && today.getDate() >= birthDate.getDate());
  // Если день рождения еще не наступил, уменьшаем возраст на 1
  if (!hasPassed) {
    age--;
  }
  // Возвращаем неотрицательный возраст
  return Math.max(0, age);
}

/**
  Вычисляет детальный возраст: годы, месяцы, дни.
  Умеет считать возраст до произвольной даты (например, до даты смерти).

  @param {string} biethDateStr - Дата рождения.
  @param {Date} endDate - конечная дата (по умолчанию - сегодня)
  @returns {object} - {year, month, days}
*/
function calculateAgeDetailed(birthDateStr, endDate = new Date()) {
  // Парсим строку с датой рождения. Если дата некорректна - возвращаем нулевые значения
  const birthDate = parseDate(birthDateStr);
  if (!birthDate) return {years: 0, months: 0, days: 0};
  // если конечная дата раньше даты рождения - возвращаем нули (возраст не может быть отрицательным)
  if (endDate < birthDate) return {years: 0, months: 0, days: 0};
  // Рассчитываем предварительную разницу между датами
  let years = endDate.getFullYear() - birthDate. getFullYear();   // Разница в годах
  let months = endDate.getMonth() - birthDate.getMonth();         // Разница в месяцах
  let days = endDate.getDate() - birthDate.getDate();             // Разница в днях

  // Корректировка, если текущий месяц ещё не достиг месяца рождения в текущем году
  // Например: рождение 15.05.2020, а текущая дата 10.05.2020 → месяцы = -1 → уменьшаем годы, добавляем 12 месяцев
  if (months < 0 || (months === 0 && days < 0)) {
    years--; // уменьшаем количество лет на 1
    months += 12; // Переводим "долг" в месяцы предыдущего года
  }

  // Корректировка дней, если день в текущем месяце ещё не наступал
  // Пример: рождение 25.03, текущая дата 15.04 → daya = -10 → берём дни из марта (последний день марта = 31)
  if (days < 0) {
    // Получаем последний день предыдущего месяца (например, для 15.04 - 31 марта)
    const lastDayPrevMonth = new Date(endDate.getFullYear(), endDate.getMonth(), 0);
    days += lastDayPrevMonth.getDate(); // Добавляем дни из предыдущего месяца
    months--; // Уменьшаем количество месяцев, так как использовали дни прошлого месяца
  }

  // Финальная корректировка месяцев (на случай, если после предыдущих шагов месяцы стали отрицательными)
  // Это маловероятно, но страхует от ошибок
  if (months < 0) {
    months += 12; // Переводим в положительные месяцы
    years--;  // Соответственно уменьшаем годы
  }
  // Возвращаем итоговый объект с кодами, месяцами и днями
  return {years, months, days};
}

/**
  Возвращает возраст ввиде короткой строки ("25 лет", "6 месяцев")
  Учитывает будущие даты и исторических личностей

  @param {string} birthDateStr - Дата рождения
  @returns {string} - Возраст строкой.
*/
function getAgeString(birthDateStr) {
  // Парсим строку с датой рождения
  const birthDate = parseDate(birthDateStr);
  // Если дата некорректна или не указана - возвращаем стандартное сообщение
  if (!birthDate) return "Дата рождения не указана";
  // Получаем текущую дату для сравнения
  const today = new Date();
  // Проверяем, относится ли год рождения к "историческим" (согласно порогу HISTORICAL_YEARS_THRESHOLD)
  if (isHistoricalYear(birthDate.getFullYear())) {
    // Вычисляем, сколько лет назад человек родился
    const yearsAgo = new Date().getFullYear() - birthDate.getFullYear();
    // Формируем строку с указанием количества лет назад и пометкой "историческая личность"
    return `${yearsAgo} ${getWordForm(yearsAgo, "year")} назад (историческая личность)`;
  }
  // Обработка случая, когда дата рождения находится в будущем
  if (birthDate > today) {
    // Рассчитываем разницу в миллисекундах между текущей датой и датой рождения
    const timeDiff = birthDate - today;
    // Переводим миллисекунды в полные дни
    const daysUntill = Math.ceil(timeDiff / (1000 * 60 * 60 * 24));
    // Если до дня рождения осталось меньше месяца
    if (daysUntill < 30) {
      return `${daysUntill} ${getWordForm(daysUntill, "day")} до рождения`;
    }
    // Если осталось меньше года - переводим дни в усреднённые месяцы (30.44 дня в месяце)
    if (daysUntill < 365) {
      const months = Math.floor(daysUntill / 30.44);
      return `${months} ${getWordForm(months, "month")} до рождения`;
    }
    // Если осталось больше года - переводим в усреднённые годы (365.25 дня в году)
    const years = Math.floor(daysUntill / 365.25);
    return `${years} ${getWordForm(years, "year")} до рождения`;
  }
  // Вычисляем возраст в полных годах (0, если день рождения ещё не наступил)
  const ageInYears = calculateAge(birthDateStr);
  // Случай, когда человек родился недавно (возраст в годах = 0)
  if (ageInYears === 0) {
    // Получаем детальный возраст (иесяцы и дни)
    const detailed = calculateAgeDetailed(birthDateStr);
    // Если прошёл хотябы 1 месяц - показать месяцы
    if (detailed.months > 0) {
      return `${detailed.months} ${getWordForm(detailed.months, "month")}`;
    }
    // Иначе показываем дни
    return `${detailed.days} ${getWordForm(detailed.days, "day")}`;
  }
  // обычный случай: возраст > 0 лет
  return `${ageInYears} ${getWordForm(ageInYears, "age")}`;
}

