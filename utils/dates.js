/**
  Возвращает русское название месяца в предложном падеже («в январе»).
  Нужно для красивых и понятных сообщений об ошибках в датах.
 
  @param {number} monthNumber - Номер месяца (1 = январь, 12 = декабрь).
  @returns {string} - Название месяца в предложном падеже.
*/
function getMonthName(monthNumber) {
  // Массив (список) названий месяцев. Индексы начинается с 0.
  const months = [
    "январе", "феврале", "марте", "апреле", "мае", "июне",
    "июле", "августе", "сентябре", "октябре", "ноябре", "декабре", 
  ];
  // monthNumber - 1, потому что в массиве январь лежит под индексом 0, а не 1.
  // Так как индексы массива начитнаются с 0, вычитаем 1 из номера чтобы месяцы шли от 1 до 12.
  // По сути делаем алиас (псевдоним) индексации с (от 0 до 11) в (от 1 до 12)
  return months[monthNumber - 1] || "неизвестном месяце";
}

/**
  Превращает двухзначный код в четырёхзначный (95 → 1995 или 25 → 2025).
  Учитывает долгожителей (старше 100 лет).

  ЛОГТКА
  - Берём текущий год, например 2026. остаток от деления на 100 = 26.
  - если введённый год ЬОЛЬШЕ 26 (например, 27) → значит это 1927 (прошлый век).
  - если МЕНЬШЕ или РАВЕН 26 (например, 25) → значит это 2025 (текущий век0.

  @param {number} year  - Год (2 или 4 цыфры).
  @returns {number} - Исправленный четырёхзначный год.
*/
function fixYearforUserList(year) {
  // Если год уже четырёхзначный (1000 и больше) - ничего не меняем.
  if (year >= 1000) {
    return year;
  }
  // Получаем текущий год и его последние цифры
  const currentYear = new Date().getFullYear(); // Текущий год - Например, 2026
  const currentShortYear = currentYear % 100;   // 2026 % 100 = 26 (последние 2 цифры)

  // Если введённый год больше последних двух цифр текущего года,
  // Значит это прошлый век (19хх), иначе - текущий (20хх).
  if (year > currentShortYear) {
    return year + 1900; // Например, 27 → 1927
  } else {
    return year + 2000; // Например, 25 → 2025
  }
}

/**
  Умный парсинг даты из строки.
  Понимает форматы: ДД-ММ-ГГГГ, ГГГГ-ММ-ДД, ДД.ММ.ГГГГ, ГГГГ.ММ.ДД и любые другие разделители.
  Проверяет дату на реальность (нет ли 31 февраля и т.д.).

  @param {string} dateStr - строка с датой.
  @returns {Date|null} - Объект Date или null, если дата некорректна.
*/
function parseDate(dateStr) {
  // ШАГ 1: Если строка пустая - возвращаем null.
  if (!dateStr) {
    return null;
  }

  // ШАГ 2: Извлекаем все последовательности цифр из строки. Регулярное выражение /\d+/g - "найти все числа".
  const numbers = dateStr.match(/\d+/g);
  // Если чисел меньше трёх, дата не полная
  if (!numbers || numbers.length < 3) {
    console.error("❌ pfrseDate: Нужно 3 числа (день, месяц, год");
    return null;
  }

  // ШАГ 3: Преобразуем первые три целые числа. По сути делаем палитру параметров дни, месяцы, годы
  const num1 = parseInt(numbers[0], 10);  // Для дней
  const num2 = parseInt(numbers[1], 10);  // Для месяцев
  let year = parseInt(numbers[2], 10);    // Для годов
  let day, month;

  // ШАГ 4: Определяем, в каком порядке идут числа.
  // Если третье число состоит из 4 цифр → то формат ДД-ММ-ГГГГ.
  if (numbers[2].length === 4) {
    day = num1;
    month = num2;
  }
  // Если первое число состоит из 4 цифр → то формат ГГГГ-ММ-ДД.
  else if (numbers[0].length === 4) {
    year = num1;
    month = num2;
    day = parseInt(numbers[2], 10);
  }
  // Иначе по умолчанию считаем, что это ДД-ММ-ГГ(ГГ).
  else {
    day = num1;
    month = num2;
  }

  // ШАГ 5: Исправляем двухзначныйгод на четырёхзначный.
  year = fixYearforUserList(year);

  // ШАГ 6: Проверки корректности дня и месяца
  // День должен быть от 1 до 31. (Берём это за основу)
  if (day < 1 || day > 31) {
    console.error(`❌ parseDate: День ${day} вне диапазона 1-31`);
    return null;
  }
  // Проверка для месяцев в которых по 30 дней. А это апрель, июнь, сентябрь, ноябрь.
  if (day === 31 && (month === 4 || month === 6 || month === 9 || month === 11)) {
    console.error(`❌ parseDate: В ${getMonthName(month)} только 30 дней`);
    return null;
  }
  // Февраль: проверяем високосность.
  if (month === 2) {
    // Определяем, високосный ли год.
    const isLeapYear = (year % 400 === 0) || (year % 4 === 0 && year % 100 !== 0);
    const maxDays = isLeapYear ? 29 : 28;
    if (day > maxDays) {
      console.error(`❌ parseDate: В феврале ${year} только ${maxDays} дней`);
      return null;
    }
  }
  
  // Месяц должен быть от 1 до 12.
  if (month < 1 || month > 12) {
    console.error(`❌ parseDate: Месяц ${month} вне диапазона от 1 до 12`);
    return null;
  }
  
  // ШАГ 7: Предупреждения о слишком старых или будущих годах (предупреждения, не ошибки).
  const currentYear = new Date().getFullYear();
  const age = currentYear - year;
  const maxHumanAge = 122;    // Возраст Жанны Луизы Кальман. Родилась 21-02-1875 г., умерла 04-08-1997 г.
  // Если возраст превышает рекордный, выводим предупреждение.
  if (age > maxHumanAge) {
    console.warn(`⚠️ parseDate: Год ${year} слишком ранний (возраст ${age} ${getWordForm(age, "year")} )`);
    // Иначе если год исторический (по плавающему порогу), выводим предупреждение.
  } else if (isHistoricalYear(year)) {
    console.warn(`⚠️ parseDate: Год ${year} очень ранний (историческая дата).
      Если человек и вправду родился в этом году, то сейчас ему ${age} ${getWordForm(age, "year")}.
      Если это так, то ОК! Если нет, то перепроверьте дату рождения`);
  }
  // Проверка на год из будущего
  if (year > currentYear + 1) {
    console.error(`❌ parseDate: Год ${year} ещё не наступил`);
    return null;
  }
  // ШАГ 8: Создаём объект Date.
  // ВАЖНО: в JavaScript месяцы нумеруются с 0 (январь = 0), поэтому вычитаем 1.
  const date = new Date(year, month - 1, day);
  // ШАГ 9: Финальная проверка: чтоб JS мог автоматически исправить несуществующую дату
  // Например, 31 февраля на 3 марта.
  if (date.getFullYear() !== year || date.getMonth() !== month - 1 || date.getDate() !== day) {
    console.error(`❌ parseDate: Дата ${day}.${month}.${year} не существует`);
    return null;
  }
  
  // Возвращаем корректный объект Date
  return date;
}

