/**
  Сколько лет должно пройти с момента события, чтобы оно считалось "историческим".
  ОБОСНОВАНИЕ ПОРОГА: Порог 122 лет соответствует максимальной задокументированной 
  продолжительности жизни человека (Жанна Кальман, 122 года, 1875-1997).
  Например, при пороге 122 лет в 2026 году историческими будут даты до 1904 года.
*/
const HISTORICAL_YEARS_THRESHOLD = 122;

/**
  Проверяет, является ли указанный год "историческим" относительно текущего.
  Используется, чтобы не хардкодить 1904 год, который со временем устареет.

  @param {number} year - Проверяемый год.
  @returns {boolean} - true, если год исторический (отстоит то текущего больше, чем на порог).
*/
function isHistoricalYear(year) {
  // Получаем текущий год (например, 2026).
  const currentYear = new Date().getFullYear();
  // Вычисляем разницу между текущем годом и проверяемым
  const difference = currentYear - year;
  // Если разница больше порога в 122 года, то год считается историческим.
  return difference > HISTORICAL_YEARS_THRESHOLD;
}

